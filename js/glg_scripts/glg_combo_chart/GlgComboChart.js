//////////////////////////////////////////////////////////////////////////////
// Scroll to the end of the data history buffer.
// All charts have XAxis/Span and XAxis/EndValue constrained, so the
// charts scroll together. TimeAxis/Span and TimeAxis/EndValue are also
// constrained to the charts' XAxis/Span and XAxis/EndValue repspectvely.
//////////////////////////////////////////////////////////////////////////////
GlgComboChart.prototype.ScrollToDataEnd =
  function( /*int*/ data_end, /*boolean*/ show_extra_sec )
{
   let end_value, extra_sec;   /* double */
    
   if( data_end == DONT_CHANGE )
     return;
    
   // Use the first chart to obtain data extent.
   let min_max =   /* GlgMinMax */
     this.ChartArray[ 0 ].chart.GetChartDataExtent( null, /*x extent*/ true,
                                                    /*query all samples*/ false ); 
   if( min_max == null )
     return;
    
   if( show_extra_sec )   
     extra_sec = this.GetExtraSeconds();
   else
     extra_sec = 0.0;
    
   if( data_end == MOST_RECENT )
     end_value = min_max.max + extra_sec;
   else   /* LEAST_RECENT */
     end_value = min_max.min - extra_sec + this.TimeSpan ;
    
   this.TimeAxis.SetDResource( "EndValue", end_value );
}

