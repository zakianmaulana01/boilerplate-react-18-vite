// GLG Combo Chart Demo
//
// This demo demonstrates how to display, configure and update a GLG realtime
// stripchart containing multiple stacked charts that share the same Time
// axis. User interaction includes cursor feedback, interactive zooming and
// scrolling, ability to select variables to be displayed by showing or hiding
// individual charts, as well as define different sets of variables via
// configuration data.
//
// The demo is written in pure HTML5 and JavaScript. The source code of the
// demo uses the GLG Toolkit JavaScript Library supplied by the included
// Glg*.js and GlgToolkit*.js files.
