////////////////////////////////////////////////////////////////////////
// The folowing properies will be added to the ChartInfo object by the
// InitializeChart method:
//   chart_vp          - stored the GLG object ID of the chart's viewport
//   chart             - stored the GLG object ID of the chart object
//   plot_info         - PlotInfo object that stored plot information
//   low, high         - double values that store YAxis ranges
//   cross_hair_line   - stores the GLG object ID of the vertical line
//                       that follows the mouse position, is stored only
//                       for the first chart.
//   label_annotation  - stores the GLG object ID of the LabelAnnotation
//                       object used to display custom tooltip showing
//                       value and time of the plot's data sample at the 
//                       mouse position.
//   marker_annotation - stores the GLG object ID of the MarkerAnnotation
//                       object used to annotate the data sample selected
//                       in the plot.
////////////////////////////////////////////////////////////////////////    
function ChartInfo( /*GlgObject*/ chart_vp )
{
}

////////////////////////////////////////////////////////////////////////
// Should be invoked after hierarchy setup.
////////////////////////////////////////////////////////////////////////
function InitializeChart( /*GlgObject*/ chart_vp,
                          /*ConfigRecord*/ config_record )
{
   if( config_record == null )
     return null;

   let chart_info = new ChartInfo();
   
   // Store the chart viewport.
   chart_info.chart_vp = chart_vp;     /* GlgObject */
   if( chart_vp == null )
   {
      AppError( "Invalid chart viewport." );
      return;
   }
   
   // Store the chart object inside the chart's viewport.
   chart_info.chart = chart_vp.GetResourceObject( "Chart" );  /*GlgObject*/
    
   // Store chart range to be able to restore it after zoom.
   chart_info.low = config_record.low;
   chart_info.high = config_record.high;
   chart_info.SetChartRange();
   
   /* Set ChartZoomMode for the viewport, in case it hasn't been set in the
      drawing.
   */
   chart_info.chart_vp.SetZoomMode( null, chart_info.chart, null,
                                    GLG.GlgZoomMode.CHART_ZOOM_MODE );
    
   // Set Y axis label.
   chart_info.chart.SetSResource( "YAxis/AxisLabelString",
                                  config_record.yaxis_label );
   // Set chart title.
   chart_info.chart_vp.SetSResource( "Title/String",
                                     config_record.chart_title );
   
   // Initialize the chart's plot.
   chart_info.InitPlot( config_record.tag, config_record.plot_color );
   
   /* Initializes label and marker annotations and stores them as
      label_annotation and marker_annotation properties of chart_info.
   */
   chart_info.InitAnnotations();
   
   // Set chart's initial visibility.
   chart_info.ShowChart( config_record.init_visibility );
   
   return chart_info;
}

////////////////////////////////////////////////////////////////////////
// Set chart's low and high range to the stored values.
// Assumes the chart has one YAxis and CommonRange=YES.
////////////////////////////////////////////////////////////////////////
ChartInfo.prototype.SetChartRange = function()
{
   this.chart.SetDResource( "YAxis/Low", this.low );
   this.chart.SetDResource( "YAxis/High", this.high );
}

////////////////////////////////////////////////////////////////////////
// Should be invoked after hierarchy setup.
////////////////////////////////////////////////////////////////////////
ChartInfo.prototype.InitPlot = function( /*String*/ tag, /*GlgPoint*/ color )
{
    /* This example assumes that each chart has one plot and one Y axis.
       The code may be extended to support multiple plots and multiple 
       Y axes as needed.
    */

    // Obtain and store object ID of a chart's plot.
    let plot_array = this.chart.GetResourceObject( "Plots" ); /* GlgObject */  
    let plot = plot_array.GetElement( 0 );                    /* GlgObject */
    this.plot_info = new PlotInfo( plot, tag, color );
}

////////////////////////////////////////////////////////////////////////
ChartInfo.prototype.InitCrossHairLine = function()
{
   let time_lines =
   this.chart.GetResourceObject( "TimeLines" ); /* GlgObject */
   if( time_lines == null )
     return;
    
   /* Retrieve CrossHairLine, used to draw a vertical crosshair line that 
      follows the mouse cursor.
   */
   this.cross_hair_line = time_lines.GetResourceObject( "CrossHairLine" );
   if( this.cross_hair_line == null )
   {
      AppError( "CrossHairLine object not found." );
      return;
   }
   
   // Disable time_line by default. It will be enabled on mouse move.
   this.cross_hair_line.SetDResource( "Enabled", 0.0 );
}

////////////////////////////////////////////////////////////////////////
ChartInfo.prototype.InitAnnotations = function()
{
   /* Retrieve annotation objects, if any. 
      LabelAnnotation moves with the time line and displays time and value 
      of the closest plot data sample. MarkerAnnotation highlights the found 
      data sample, if any.
    */
   let annotations =
     this.chart.GetResourceObject( "Annotations" ); /* GlgObject */    
   if( annotations == null )
     return;
    
   this.label_annotation = annotations.GetResourceObject( "LabelAnnotation" );
   if( this.label_annotation != null )
     // Disable label annotation by default.
     this.label_annotation.SetDResource( "Enabled", 0.0 );
   else
     AppError( "LabelAnnotation object not found." );
    
   this.marker_annotation = annotations.GetResourceObject( "MarkerAnnotation" );
   if( this.marker_annotation != null )
     // Disable marker annotation by default.
     this.marker_annotation.SetDResource( "Enabled", 0.0 );
   else
     AppError( "MarkerAnnotation object not found." );
}

////////////////////////////////////////////////////////////////////////
// Show/hide invididual chart.
////////////////////////////////////////////////////////////////////////
ChartInfo.prototype.ShowChart = function ( /*bool*/ show )
{
   this.chart_vp.SetDResource( "Visibility", show ? 1.0 : 0.0 );
}

////////////////////////////////////////////////////////////////////////
function PlotInfo( /*GlgObject*/ plot, /*String*/ tag, /*{r,g,b}*/ color )
{
   this.plot = plot;   // Store object ID of the chart's plot (Plot#0). */    
   this.tag = tag;     // Store data tag associated with the chart's plot.
    
   // Store plot's ValueEntryPoint.
   this.value_ep = /*GlgObject*/
        this.plot.GetResourceObject( "ValueEntryPoint" );

   // Store plot's TimeEntryPoint.
   this.time_ep = /* GlgObject */
     this.plot.GetResourceObject( "TimeEntryPoint" );

   // Store plot's ValidEntryPoint.
   this.valid_ep = /* GlgObject */
     this.plot.GetResourceObject( "ValidEntryPoint" );
        
   // Set plot's line color.
   if( color != null )
     this.plot.SetGResource( "EdgeColor", color.r, color.g, color.b );
}

