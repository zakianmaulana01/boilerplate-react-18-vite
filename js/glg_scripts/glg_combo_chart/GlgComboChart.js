//////////////////////////////////////////////////////////////////////////////
// Handle user interaction.
// Each ChartViewport% in ChartSeries has OwnsInputCB=true, so
// InputCallback is invoked with the viewport ID of the ChartViewport%
// where an input event occurred, such as chart zoom or drag event.
//////////////////////////////////////////////////////////////////////////////
GlgComboChart.prototype.InputCallback =
  function( /*GlgObject*/ viewport, /*GlgObject*/ message_obj )
{
   if( !this.Active )
     return;
    
   let origin = message_obj.GetSResource( "Origin" );
   let format = message_obj.GetSResource( "Format" );
   let action = message_obj.GetSResource( "Action" );
   let subaction = message_obj.GetSResource( "SubAction" );

   // Handle window closing.
   if( format == "Window" && action == "DeleteWindow" )
   {
      if( origin == "ChartDialog" )
      {
         // Hide ChartDialog.
         ShowDialog( this.ChartDialog, false );
         viewport.Update();
      }
   }
    
   // Process button events.
   else if( format == "Button" )
   {	 
      if( action != "Activate" &&        /* Not a push button */
          action != "ValueChanged" )     /* Not a toggle button */
        return;
        
      // Abort ZoomTo mode, if any.
      this.AbortZoomTo();
        
      if( origin == "ToggleAutoScroll" )
      {         
         /* Set Chart AutoScroll based on the ToggleAutoScroll 
            toggle button setting.
         */
         this.ChangeAutoScroll( -1 ); 
      }
      if( origin == "ToggleYDragging" )
      {         
         /* Enable/Disable Y Dragging based on the ToggleYDragging toggle 
            button setting.
         */
         this.ChangeYDragging( -1 ); 
      }
      else if( origin == "ZoomTo" )
      {
         /* Start ZoomTo operation. Wait for the mouse click in the
            TraceCallback to activate ZoomTo for the selected chart 
            that received the click.
         */
         this.StartZoomToMode = true;
      }
      else if( origin == "ResetZoom" )
      {         
         // Set initial time span and reset initial Y ranges.
         this.SetChartSpan( this.SpanIndex );  
         this.RestoreInitialYRanges();

         // Disable Y Dragging on zoom reset (1:1 button).
         this.ChangeYDragging( 0 );
      }
      else if( origin == "ScrollBack" || origin == "ScrollForward" )
      {
         this.ChangeAutoScroll( 0 );
            
         let end_value = /* double */
           this.TimeAxis.GetDResource( "EndValue" ); 

         if( origin == "ScrollBack" )
           end_value -= this.ScrollIncrement;
         else
           end_value += this.ScrollIncrement;

         this.TimeAxis.SetDResource( "EndValue", end_value );
      }
      else if( origin == "ScrollToRecent" )
      {
         // Scroll to show most recent data.
         this.ScrollToDataEnd( MOST_RECENT, true );
      }
      else if( origin == "ShowHideCharts" )
      {
         /* Display ChartDialog when the user clicked on the
            Configure toolbar button.
         */
         ShowDialog( this.ChartDialog, true );
      }
        
      this.MainViewport.Update();  // format="Button"
   }

   else if( format == "Option" )
   {
      if( action != "Select" )
        return;
        
      // Abort ZoomTo mode, if any.
      this.AbortZoomTo();

      /* Handle events from the SpanSelector menu allowing to select time 
         interval for the Time axis.
      */
      if( origin == "SpanSelector" )    /* Span change */
      { 
         this.SpanIndex =
           Math.trunc( message_obj.GetDResource( "SelectedIndex" ) );
            
         this.SetChartSpan( this.SpanIndex );
         this.RestoreInitialYRanges(); // Restore in case the chart was zoomed.
            
         /* Scroll to show the recent data to avoid showing an empty chart
            if user scrolls too much into the future or into the past.
            Invoke ScrollToDataEnd() even if AutoScroll is True to 
            scroll ahead by a few extra seconds to show a few next updates
            without scrolling the chart.
         */
         let min_max =    /* GlgMinMax */
           this.ChartArray[0].chart.GetChartDataExtent( null, /*x extent*/ true,
                                                        /*query all samples*/ false );
            
         if( min_max != null )
         {
            let first_time_stamp = min_max.min;   /* double */
            let last_time_stamp = min_max.max;    /* double */
            let displayed_time_end =    /* double */
              this.TimeAxis.GetDResource( "EndValue" );
                
            if( this.AutoScroll != 0 )
              this.ScrollToDataEnd( MOST_RECENT, true );
                
            else if( displayed_time_end >
                     last_time_stamp + this.GetExtraSeconds() )
              this.ScrollToDataEnd( MOST_RECENT, true );
                
            else if( displayed_time_end - this.TimeSpan <= first_time_stamp )
              this.ScrollToDataEnd( LEAST_RECENT, true );
                
            this.MainViewport.Update();
         }
      }
   }

   // Handle events from GlgMenu.
   else if( format == "Menu" )
   {
      if( origin == "ChartMenu" )
      {
         // Handle events from the ChartMenu inside ChartDialog.
         let button_index = /* int */
           Math.trunc( message_obj.GetDResource( "SelectedIndex" ) );
         let menu = message_obj.GetResourceObject( "Object" ); /* GlgObject */
         this.ProcessChartMenuSelection( menu, button_index );
      }
        
      this.MainViewport.Update();
   }
    
   // Handle custom MouseClick events.
   else if( format == "CustomEvent" )
   {
      let event_label = message_obj.GetSResource( "EventLabel" ); /* String */
      if( event_label == null )
        return;
          
      /* Hide ChartDialog when the user clicked on the Close button inside 
         the dialog (Close button has Custom MouseEvent with 
         EventLabel="CloseChartDialog").
      */
      if( event_label == "CloseChartDialog" )
        ShowDialog( this.ChartDialog, false );

      this.MainViewport.Update();
   }
    
   else if( format == "Chart" && action == "CrossHairUpdate" )
   {
      /* To avoid slowing down real-time chart updates, invoke Update() 
         to redraw cross-hair only if the chart is not updated fast 
         enough by the timer.
      */
      if( UPDATE_INTERVAL > 100 )
        this.viewport.Update();         
   }          
    
   else if( action == "Zoom" )    // Zoom events
   {
      if( subaction == "Start" )
      {
         // Store current AutoScroll state to restore it if ZoomTo is aborted.
         this.StoredScrollState = this.AutoScroll;
      }
      else if( subaction == "ZoomRectangle" )
      {
         // Stop scrolling when ZoomTo action is started.
         this.ChangeAutoScroll( 0 );
         this.MainViewport.Update();
      }
      else if( subaction == "End" )
      {
         /* No additional actions on finishing ZoomTo. The Y scrollbar 
            appears automatically if needed: it is set to GLG_PAN_Y_AUTO. 
            Don't resume scrolling: it'll scroll too fast since we zoomed 
            in. Keep it still to allow inspecting zoomed data.
         */
      }
      else if( subaction == "Abort" )
      {
         // Resume scrolling if it was on.
         this.ChangeAutoScroll( this.StoredScrollState ); 
         this.MainViewport.Update(); 
     }
   }
   else if( action == "Pan" )                  // Pan events
   {
      /* This code may be used to perform custom action when dragging the 
         chart's data with the mouse.
      */
      if( subaction == "Start" )              // Chart dragging start
      {
      }
      else if( subaction == "Drag" )          // Dragging
      {
         /* Refresh display when the chart is dragged to ensure the cross-hair
            line and values in annotations coinside with the cursor position.  
         */
         this.MainViewport.Update();
      }
      else if( subaction == "ValueChanged" )   // Scrollbars
      {
      }
      /* Dragging ended or aborted. */
      else if( subaction == "End" || subaction == "Abort" )
      {
      }
   }
}

//////////////////////////////////////////////////////////////////////////////
// Used to obtain coordinates of the mouse click, as well as handle native
// low level events as needed.
//////////////////////////////////////////////////////////////////////////////
GlgComboChart.prototype.TraceCallback =
  function( /*GlgObject*/ viewport, /*GlgTraceData*/ trace_info )
{
   if( !this.Active )
     return;

   // Obtain the viewport where the event occurred.
   let event_vp = trace_info.viewport;            /* GlgObject */
   let vp_name = event_vp.GetSResource( "Name" ); /* String */
    
   let event_type = trace_info.event_type;        /* GlgEventType */
   let x, y;                                      /* double */
   switch( event_type )
   {
    case GLG.GlgEventType.TOUCH_START:
      if( vp_name.startsWith( "ChartViewport" ) )
        GLG.SetTouchMode();     /* Enable chart dragging via touch events. */
      /* Fall through */
       
    case GLG.GlgEventType.TOUCH_MOVED:
      if( !GLG.GetTouchMode() )
        return;
      /* falls through */
      
    case GLG.GlgEventType.MOUSE_PRESSED:
    case GLG.GlgEventType.MOUSE_MOVED:
      // Retrieve mouse coordinates.
      x = trace_info.mouse_x * this.CoordScale;
      y = trace_info.mouse_y * this.CoordScale;
         
      /* COORD_MAPPING_ADJ is added to the cursor coordinates for precise
         pixel mapping.
      */
      x += GLG.COORD_MAPPING_ADJ;
      y += GLG.COORD_MAPPING_ADJ;
      break;

    default: break;
   }
   
   switch( event_type )
   {
    case GLG.GlgEventType.TOUCH_START:
    case GLG.GlgEventType.MOUSE_PRESSED:
      /* Handle only events that occur in one of the chart viewports
         with the name beginning with "ChartViewport".
      */
      if( !vp_name.startsWith( "ChartViewport" ) )
        return;

      if( this.StartZoomToMode )
      {
         event_vp.SetZoom( null, 't', 0.0 );
         this.StartZoomToMode = false;
         return;  // ZoomTo or dragging mode in progress.
      }

      // If ZoomTo mode is in progress for any of the charts, do nothing else.
      let zoom_vp = this.ZoomToMode();  /* GlgObject */
      if( zoom_vp != null )
        return; // ZoomTo or dragging mode in progress.

      // Start scrolling on mobile devices only in ScrollMode.
      if( !this.ScrollMode )
        return;
      
      /* Start dragging with the mouse when the user clicked in one of the 
         chart areas. If Y dragging is enabled, dragging is activated 
         in both X and Y direction ('s'). Otherwise, dragging is activated 
         only in X direction ('>').
      */
      let drag_type = ( this.EnableYDragging == 1 ? 's' : '>' ); 
      event_vp.SetZoom( null, drag_type, 0.0 );
      
      // Disable AutoScroll not to interfere with dragging.
      this.ChangeAutoScroll( 0 );
      this.MainViewport.Update();
      break;
        
    case GLG.GlgEventType.TOUCH_MOVED:
    case GLG.GlgEventType.MOUSE_MOVED:
      /* Handle only events that occur in one of the chart viewports 
         with the name beginning with "ChartViewport", or in the 
         top level $Widget viewport that contains TimeAxis.
      */
      if( !vp_name.startsWith( "ChartViewport" ) &&
          !vp_name.startsWith( "$Widget" ) )
      {
         this.CursorPosition = null;
         return;
      }
        
      // Store cursor position.
      this.CursorPosition = GLG.CreateGlgPoint( x, y, 0.0 );
        
      /* If the mouse moves along the Time axis (which is a child of 
         $Widget), convert cursor position relatively to one of the 
         chart viewports, since we later use the chart (not TimeAxis)
         to obtain a time stamp at cursor. Since we take into consideration
         only horizontal position of the cursor, and all charts are aligned
         vertically and have the same width, we can convert the coordinates
         relatively to the first chart viewport. The cursor X coordinate in
         the $Widget viewport differs from X coordinate in the chart 
         viewports by the border width of the chart viewport, therefore 
         the cursor position needs to be converted to the chart viewport.
      */
      if( vp_name.startsWith( "$Widget" ) )  
        GLG.TranslatePointOrigin( event_vp, this.ChartArray[0].chart_vp,
                                  this.CursorPosition );
        
      /* Display vertical time line at cursor position if there is no
         zooming or dragging mode in progress.
      */
      if( this.ZoomToMode() == null ) 
        this.DisplayCrossHairLine( this.CursorPosition );
      break;
                
    case GLG.GlgEventType.MOUSE_EXITED:
      this.CursorPosition = null;
      this.DisplayCrossHairLine( this.CursorPosition ); // Erase crosshair.
      break;
        
    default: break;
   }
}

