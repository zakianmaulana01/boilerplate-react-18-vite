////////////////////////////////////////////////////////////////////////////// 
// Script entry point: Starts the combo chart by loading its drawing.
////////////////////////////////////////////////////////////////////////////// 
GlgComboChart.prototype.Start = function()
{
   Debug( "Starting: " + this.GLG_div_name );

   this.Active = true;

   // Set initial size of the drawing.
   this.SetDrawingSize( false );

   // Show touch action selector for mobile devices.
   if( this.IsMobile )
     ShowElement( this.GLG_div_name + "_touch_action", true );
   
   /* Load assets, including:
      - GLG scrollbars (.g files).
      - GLG chart drawing, using filename defined by DRAWING_NAME.
 
      When all three assets are loaded, LoadConfiguration callback is invoked 
      to load chart configuration specified by INIT_CONFIGURATION_INDEX.

      If USE_CONFIG_FILE=true, configuration data are loaded from the specified
      file in JSON format defined by the configuration's config_file. 

      If USE_CONFIG_FILE=false, configuration data are loaded from the 
      hardcoded table defined by the configuration's config_table.

      After configuration data finished loading, the charts in the drawing get 
      configured based on the loaded configuration.
   */
   this.LoadAssets( ()=>this.LoadConfigurationFrom( INIT_CONFIGURATION_INDEX ),
                    null );
}

////////////////////////////////////////////////////////////////////////////// 
// Script entry point: Performs cleanup.
////////////////////////////////////////////////////////////////////////////// 
GlgComboChart.prototype.Cleanup = function()
{
   Debug( "Cleanup for: " + this.GLG_div_name );
   
   this.Active = false;    // Ignore any pending updates and callbacks.

   if( this.MainViewport )
     this.MainViewport.ResetHierarchy();   // Undisplay GLG drawing.

   if( this.DataFeed )
     this.DataFeed.Cleanup();              // Cleanup DataFeed.

   this.AbortPendingLoadRequests();
    
   if( this.ResizeListener )
     window.removeEventListener( "resize", this.ResizeListener );
}

////////////////////////////////////////////////////////////////////////////// 
GlgComboChart.prototype.GetFullName = function( drawing_name )
{
   if( this.GlgPath == null )
     return drawing_name;

   return this.GlgPath + "/" + drawing_name;
}

////////////////////////////////////////////////////////////////////////////// 
// Loads the configuration specified by the index parameter.
// It is also used to respond to the HTML button clicks.
////////////////////////////////////////////////////////////////////////////// 
GlgComboChart.prototype.LoadConfigurationFrom = function( /*int*/ index )
{
   /* Data object containing information about the chart configuration to be 
      loaded:
      - config_file  : filename of the configuration file in JSON format. 
                       Used if USE_CONFIG_FILE=true.
      - config_table : hardcoded configuration table. Used if 
                       USE_CONFIG_FILE=false or config_file=null.
      - config_title : Configuration title to be displayed in the status field.
   */
   let load_data;

   switch( index )
   {
    case 1:
      load_data = { config_file: CONFIGURATION1_FILENAME,
                    config_table: ConfigDataTable1,
                    config_title: CONFIGURATION1_TITLE };
      this.LoadConfiguration( load_data );
      break;

    case 2:
      load_data = { config_file: CONFIGURATION2_FILENAME,
                    config_table: ConfigDataTable2,
                    config_title: CONFIGURATION2_TITLE };
      this.LoadConfiguration( load_data );
      break;

    default: AppError( "Invalid configuration button index." ); break;
   }
}

////////////////////////////////////////////////////////////////////////////////
// Loads chart configuration data. On start-up, this function is invoked as a
// callback by LoadAssets() after the chart drawing is loaded. This function can
// be also invoked explicitly to load new configuration data and reconfigure the
// charts dynamically at run-time without reloading the drawing. The example
// invokes this function when the user clicks on one of the HTML buttons:
// Configuration1 or Configuration2.
////////////////////////////////////////////////////////////////////////////////
GlgComboChart.prototype.LoadConfiguration = function( /*Object*/ config_info )
{
   Debug( "LoadConfiguration: " + config_info.config_title +
          " for: " + this.GLG_div_name );
  
   if( !this.Active )
     return;

   // Disable load requests from HTML buttons until all assets have been loaded.
   if( !this.AssetsLoadedFlag )
     return;

   if( config_info == null )
   {
      AppError( "LoadConfiguration: Null config_info." );
      return;
   }
   
   let filename = config_info.config_file;
   let config_table = config_info.config_table;
   
   if( filename == null && config_table == null )
   {
      AppError( "LoadConfiguration: Invalid configuration information." );
      return;
   }
   
   /* If the requested configuration matches currently loaded configuration, 
      do nothing to avoid loading the same configuration.
   */
   if( this.SameConfiguration( config_info ) )
     return;

   // Load new configuration.

   // Abort pending load requests, if any.
   this.AbortPendingLoadRequests();    
   
   // Create a new load request.
   this.ConfigLoadRequest = { enabled: true, config_info: config_info };
   
   // Display title of the new configuration load request.
   this.NewConfigTitle = config_info.config_title;   
   this.DisplayStatus();
   
   if( USE_CONFIG_FILE && filename )
   {
      /* Load new configuration from file.
         the LoadConfigCB callback will be invoked when the drawing has been 
         loaded.

         Using "bind( this )" as a shorter way to provide "this" compared with 
         using lambda: with bind, we do not need to specify parameter list that
         we would need to provide for lambda.
      */
      GLG.LoadAsset( this.GetFullName( filename ),
                     GLG.GlgHTTPRequestResponseType.JSON,
                     /*callback*/ this.LoadConfigCB.bind( this ),
                     /*user data*/ this.ConfigLoadRequest );
   }
   else
     /* Load new configuration from a hardcoded configuration data table.
        Invoke LoadConfigCB on a timeout to let the browser update status 
        display to indicate a new configuration is loaded.
     */
     setTimeout( this.LoadConfigCB( config_table,
                                    this.ConfigLoadRequest ).bind( this ), 30 );
       
}

////////////////////////////////////////////////////////////////////////
// This callback is invoked when a configuration file finished loading.
////////////////////////////////////////////////////////////////////////
GlgComboChart.prototype.LoadConfigCB =
  function( /*ConfigRecord[]*/ config_data, /*Object*/ user_data )
{
   if( !this.Active )
     return;
      
   let load_request = user_data;
   if( !load_request.enabled )
     /* This load request was aborted by requesting to load another 
        configuration before this load request has finished.
     */
     return;

   if( config_data == null || config_data.length == 0 )
   {
      /* Keep previously loaded configuration (if any), display status info and 
         generate an error.
      */
      AppError( "Configuration loading failed: " + this.NewConfigTitle );
      this.NewConfigTitle = null;
      this.DisplayStatus();
      return;
   }
   
   // Configuration data are loaded: disable spinning loader.
   RemoveElement( this.GLG_div_name, "loader_container" );

   // Store currently loaded configuration information.
   this.LoadedConfigInfo = load_request.config_info;
   
   // Reset: we are done with this request.
   this.ConfigLoadRequest = null;
   
   // Clear current configuration, if any.
   this.ClearConfiguration();

   // Store new configuration.
   this.Configuration = config_data;

   // Start GLG chart based on loaded configuration. 
   this.StartComboChart();

   // Update status info: remove "Loading ..."
   this.NewConfigTitle = null;   
   this.DisplayStatus();
}

//////////////////////////////////////////////////////////////////////////////
GlgComboChart.prototype.StartComboChart = function()
{
   if( this.Configuration == null )
   {
      AppError( "Invalid chart configuration." );
      return;
   }
   
   /* Set number of charts before hierarchy setup based on the loaded 
      configuration.
   */
   if( !this.ConfigureChartsBeforeH() )
      return;
   
   // Setup object hierarchy in the drawing.
   this.MainViewport.SetupHierarchy();
   
   // Store current time.
   this.CurrentTime = GetCurrTime();

   // Demo only: Initialize LastTime to the current time.
   this.LastTime = this.CurrentTime;
   
   // Configure charts after hierarchy setup.
   this.ConfigureChartsAfterH();

   // Display the drawing on a web page.
   this.MainViewport.Update();

   /* Set the flag to indicate the charts are configured and ready for
      data updates.
   */
   this.Ready = true;

   // Start dynamic updates upon startup.
   if( this.FirstTime )
   {
      this.FirstTime = false;   // First time: reset the flag.   
      this.GetData();
   }
}

//////////////////////////////////////////////////////////////////////////////
// Initialization performed upon drawing loading. Invoked from AssetLoadedCB.
//////////////////////////////////////////////////////////////////////////////
GlgComboChart.prototype.Init = function( /*GlgObject*/ drawing )
{
   if( drawing == null )
   {
      AppAlert( "Can't load drawing, check console message for details." );
      return;
   }
   
   // Add DataFeed object used to animate the drawing with real-time data.
   this.AddDataFeed();
   
   // Store loaded drawing viewport.
   this.MainViewport = drawing;
   
   // Define the HTML element in a web page to display the drawing.
   drawing.SetParentElement( this.GLG_div_name );
   
   // Disable viewport border to use the border of the glg_area.
   if( this.Standalone )
     drawing.SetDResource( "LineWidth", 0 );
   
   // Add Input and Trace callbacks.
   this.MainViewport.AddListener( GLG.GlgCallbackType.INPUT_CB,
                                  this.InputCallback.bind( this ) );
   this.MainViewport.AddListener( GLG.GlgCallbackType.TRACE_CB,
                                  this.TraceCallback.bind( this ) );
    
   // Retrieve object ID of the Toolbar viewport.
   this.Toolbar = this.MainViewport.GetResourceObject( "Toolbar" );
   if( this.Toolbar == null )
     AppError( "Toolbar object not found. " );

   // Initialize ChartDialog and ChartMenu.
   this.InitChartDialog();
    
   // Store object ID of the Time axis.
   this.TimeAxis = this.MainViewport.GetResourceObject( "TimeAxis" );

   /* Enable automatic layout adjustment for the ChartSeries when
      individual charts change their visibility: if one of the charts
      becomes invisible, the height of the other charts is adjusted
      automatically to occupy the vacant space.
   */
   this.MainViewport.SetDResource( "ChartSeries/HandleInvisible",
                                   GLG.GlgHandleInvisible.SHIFT_ROWS );
   this.MainViewport.SetDResource( "ChartSeries/FillSpace",
                                   GLG.GlgFillSpace.ADJUST_ROW_HEIGHT );
}

////////////////////////////////////////////////////////////////////////
// Initialize ChartDialog and ChartMenu variables.
// Set ChartDialog size and position, as well as make it a floating dialog.
////////////////////////////////////////////////////////////////////////
GlgComboChart.prototype.InitChartDialog = function()
{
   /* Store object ID of the ChartDialog viewport used to select variables
      to be plotted.
   */
   this.ChartDialog = this.MainViewport.GetResourceObject( "ChartDialog" );
   if( this.ChartDialog == null )
   {
       AppInfo( "ChartDialog not found." );
       return;
   }

   // Store object ID of the ChartMenu inside ChartDialog.
   this.ChartMenu = this.ChartDialog.GetResourceObject( "ChartMenu" );
   if( this.ChartMenu == null )
     AppError( "ChartMenu not found." );

   // Make it a floating dialog.
   this.ChartDialog.SetDResource( "LineWidth", 0.0 );
   this.ChartDialog.SetDResource( "ShellType", GLG.GlgShellType.DIALOG_SHELL );

   // Set dialog title. 
   this.ChartDialog.SetSResource( "ScreenName", "Chart Selection" );
   
   // Set dialog size and position.
   this.SetDialogSizeAndPosition( this.ChartDialog );
      
   // Make the dialog visible on start up.
   ShowDialog( this.ChartDialog, true );
}
   
//////////////////////////////////////////////////////////////////////////////
// Create new ChartArray based on the stored Configuration (if any).
// Must be invoked after hierarchy setup.
//////////////////////////////////////////////////////////////////////////////
GlgComboChart.prototype.ConfigureChartsAfterH = function()
{
   if( this.MainViewport == null ||
       this.Configuration == null || this.NumCharts == 0 )
   {
      AppError( "Chart configuration failed." );
      this.ClearConfiguration();
      return;
   }
    
   // Retrieve ChartSeries object.
   let chart_series = /* GlgObject */
     this.MainViewport.GetResourceObject( "ChartSeries" );    
   if( chart_series == null )
   {
      AppError( "ChartSeries not found. Chart configuration failed." );
      this.ChartArray = null;
      return;
   }
    
   /* Create new ChartArray and populate it with chart information based 
      on the provided chart configuration.
   */
   this.ChartArray = [];
   for( let i=0; i<this.NumCharts; ++i )
   {
      // Retrieve objectID of each chart's viewport.
      let chart_vp = /* GlgObject */
        chart_series.GetResourceObject( "ChartViewport" + i );

      // Creates and fills a ChartInfo record.
      let chart_info = InitializeChart( chart_vp, this.Configuration[i] );

      /* Initialize vertical line that moves with the mouse and stores
         its object ID as the cross_hair_line property of chart_info.
         This can be done only for the first chart, since attributes of 
         all vertical lines in all charts are constrained.
      */
      if( i == 0 )
        chart_info.InitCrossHairLine();
      
      this.ChartArray.push( chart_info );
   }
    
   // Configure Chart menu in the ChartDialog, if any.
   this.InitChartMenuAfterH();

   /* Populate SpanSelector option menu with items based on the SpanData array.
      The menu allows to select a desired time span for the TimeAxis.
      Menu initialization must be done after hierarchy setup.
      
      The demo uses the same SpanData array for both Configuration1 and
      Confgiguration2, but the application may extend the code to supply 
      a different SpanData array for each configuration. 
   */
   this.InitSpanSelector();
 
   // Enable AutoScroll, both for the toggle button and the chart.
   this.ChangeAutoScroll( 1 );

   // Disable Y Dragging by default.
   this.ChangeYDragging( 0 );

   // Set time span of the TimeAxis.
   this.SetChartSpan( this.SpanIndex );
    
   // Store valid tag sources in DataTagList array.
   this.CreateDataTagList();

   if( PREFILL_DATA )
     this.PrefillChartsWithData();
}

////////////////////////////////////////////////////////////////////////
GlgComboChart.prototype.PrefillChartsWithData = function()
{
   if( this.NumCharts == 0 )
     return;

   /* Prefill each chart's history buffer with data. The demo prefills
      the buffer with data up to the current time.
      */
   for( let i=0; i<this.NumCharts; ++i )
     this.FillChartHistory( this.ChartArray[i] );
}

//////////////////////////////////////////////////////////////////////////////
// Create DataFeed object used to animate the chart with data.
// Simulated data are provided by DemoDataFeed. The application should provide
// a custom implementation of LiveDataFeed.js to supply real-time application
// data for the chart.
//////////////////////////////////////////////////////////////////////////////
GlgComboChart.prototype.AddDataFeed = function()
{
   if( RANDOM_DATA )
   {
      this.DataFeed = new DemoDataFeed( this );
      AppLog( "Using simulated DemoDataFeed." );
   }
   else
   {
      this.DataFeed = new LiveDataFeed();
      AppLog( "Using LiveDataFeed." );
   }
}

//////////////////////////////////////////////////////////////////////////////
// Create DataTagList array containing a list of all tags for the chart plots.
// This list is passed by DataFeed.ReadData to the server to request new
// data for the tags of interest. This list gets recreated whena new chart
// configuration is loaded.
//////////////////////////////////////////////////////////////////////////////
GlgComboChart.prototype.CreateDataTagList = function()
{
   if( this.ChartArray == null || this.ChartArray.length == 0 )
   {
      this.DataTgagList = null;
      return;
   }

   this.DataTagList = [];   /* String[] */
   for( let i=0; i<this.ChartArray.length; ++i )
   {
      // Store only valid tag sources, skipping unassigned tags.
      if( !IsUndefined( this.ChartArray[i].plot_info.tag ) )
        this.DataTagList.push( { tag: this.ChartArray[i].plot_info.tag } );
   }
}

////////////////////////////////////////////////////////////////////////
// Chart initialization before hierarchy setup.
// Set the number of stacked charts. The number of charts may be changed 
// dynamically at run-time.
////////////////////////////////////////////////////////////////////////
GlgComboChart.prototype.ConfigureChartsBeforeH = function() /* boolean */
{
   if( this.MainViewport == null )
     return false;

   let chart_series = this.MainViewport.GetResourceObject( "ChartSeries" );
   if( chart_series == null )
   {
      AppError( "ChartSeries not found, SetNumCharts failed." );
      this.NumCharts = 0;
      return false;
   }
   
   // Set number of charts in the drawing.
   this.NumCharts = this.Configuration.length;
   chart_series.SetDResource( "NumCharts", this.NumCharts );   
   
   /* Set BUFFER_SIZE for the series' template before hierarchy setup. This
      setting will be inherited by all series' instances (individual charts)
      when the instances get created during hierarchy setup.
   */
   chart_series.SetDResource( "ChartViewport/Chart/BufferSize", BUFFER_SIZE );
   
   /* Set number of menu items in the ChartDialog to match the number
      of charts.
   */
   this.InitChartMenuBeforeH();
   
   return true;
}

//////////////////////////////////////////////////////////////////////////////
// Set number of rows in the ChartMenu inside ChartDialog to match the
// number of charts. This function is invoked before hierarchy setup.
//////////////////////////////////////////////////////////////////////////////
GlgComboChart.prototype.InitChartMenuBeforeH = function()
{
   if( this.ChartMenu == null || this.NumCharts <= 0 )
     return;

   // Set number of menu rows.
   this.ChartMenu.SetDResource( "NumRows", this.NumCharts );

   // Adjust number of visible rows.
   this.ChartMenu.SetDResource( "NumVisibleRows", this.ChartMenuNumVisRows );
}

/////////////////////////////////////////////////////////////////////////
// Popuplate ChartMenu inside ChartDialog using data from Configuration[]
// array. This function is invoked after hierarchy setup.
/////////////////////////////////////////////////////////////////////////
GlgComboChart.prototype.InitChartMenuAfterH = function()
{
   if( this.ChartMenu == null )
     return;
    
   /* Populate chart menu with labels using Configuration array and set
      the state of each toggle button based on the specified initial visibility.
   */
   for( let i=0; i<this.NumCharts; ++i )
   {
      let button =  /* GlgObject */
        this.ChartMenu.GetResourceObject( "Button" + i );
      button.SetSResource( "LabelString", this.Configuration[i].chart_title );
      button.SetDResource( "OnState",
                           this.Configuration[i].init_visibility ? 1 : 0 );
   }
}

