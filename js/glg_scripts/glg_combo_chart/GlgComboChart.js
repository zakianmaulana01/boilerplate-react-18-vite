//////////////////////////////////////////////////////////////////////////////
// Return true if the requested configuration config_info is the same
// as curerntly loaded configuration LoadedConfigInfo. Otherwise, returns false.
//////////////////////////////////////////////////////////////////////////////
GlgComboChart.prototype.SameConfiguration =
  function( /*Object*/ config_info ) /* boolean */
{
   let filename = config_info.config_file;
   let config_table = config_info.config_table;
   
   if( !this.LoadedConfigInfo )
     return false;
   
   if( USE_CONFIG_FILE && filename )
     return filename == this.LoadedConfigInfo.config_file;
   else
     return ( config_table != null &&
              config_table == this.LoadedConfigInfo.config_table );
}

