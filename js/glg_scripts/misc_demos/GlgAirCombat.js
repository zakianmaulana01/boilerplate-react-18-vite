//////////////////////////////////////////////////////////////////////////
GlgAirCombat.prototype.DisplaySelectedPlaneInfo = function()
{
   if( this.SelectedPlaneIndex == -1 )
     return;

   let plane = this.Planes[ this.SelectedPlaneIndex ];
   
   let roll = plane.angle.x;
   let pitch = plane.angle.y;
   let yaw = plane.angle.z - 90.0;
   
   while( roll > 180.0 )
     roll -= 360.0;
   while( pitch > 180.0 )
     pitch -= 360.0;
   while( yaw > 180.0 )
     yaw -= 360.0;
   
   this.InfoDialog.SetDResource( "Roll",  roll );
   this.InfoDialog.SetDResource( "Pitch", pitch );
   this.InfoDialog.SetDResource( "Yaw",   yaw );
   
   let lat_string = CreateLocationString( plane.position.y, true );
   let lon_string = CreateLocationString( plane.position.x, false );

   this.InfoDialog.SetSResource( "Lat", lat_string);
   this.InfoDialog.SetSResource( "Lon", lon_string );
}

//////////////////////////////////////////////////////////////////////////
GlgAirCombat.prototype.SelectPlane = function( /* int */ plane_index )
{
   if( this.SelectedPlaneIndex == -1 )
     // No previously selected plane: activate an information popup dialog.
     this.InfoDialog.SetDResource( "Visibility", 1.0 );
   else 
   {      
      /* The dialog is already active, just unselect the previously selected 
         plane.
      */
      let curr_selected_plane = this.Planes[ this.SelectedPlaneIndex ];
      if( !curr_selected_plane.destroyed )
        curr_selected_plane.glg_object.SetDResource( "SelectedState", 0.0 );
   }
      
   this.SelectedPlaneIndex = plane_index;
   let selected_plane = this.Planes[ plane_index ];
   
   // Display selected plane or missile info stored as its name.
   let plane_name = selected_plane.glg_object.GetSResource( "Name" );
   this.InfoDialog.SetSResource( "PlaneName", plane_name );

   // Set the SelectedState resource which controls an icon's color. 
   selected_plane.glg_object.SetDResource( "SelectedState", 1.0 );
}

//////////////////////////////////////////////////////////////////////////
GlgAirCombat.prototype.UnselectPlane = function()
{
   if( this.SelectedPlaneIndex == -1 )
     return;

   let selected_plane = this.Planes[ this.SelectedPlaneIndex ];   
   if( !selected_plane.destroyed )
     selected_plane.glg_object.SetDResource( "SelectedState", 0.0 );
   
   this.InfoDialog.SetDResource( "Visibility", 0.0 );
   this.SelectedPlaneIndex = -1;
}

//////////////////////////////////////////////////////////////////////////
// Processes buttons' and sliders' events.
//////////////////////////////////////////////////////////////////////////
GlgAirCombat.prototype.InputCallback =
  function( /* GlgObject */ vp, /* GlgObject */ message_obj )
{
   if( !this.Active )
     return;     
    
   let origin = message_obj.GetSResource( "Origin" );
   let format = message_obj.GetSResource( "Format" );
   let action = message_obj.GetSResource( "Action" );

   if( format == "Slider" )
   {
      if( action != "ValueChanged" )
        return;
      
      if( origin == "UpdateSpeed" )
        // Query the new value of the UpdateSpeed Slider (relative value).
        this.UpdateSpeed = message_obj.GetDResource( "ValueX" ); 
   }
   else if( format == "Button" )
   {
      if( action != "Activate" )
        return;
      
      if( origin == "Restart" )
      {
         /* Delete all planes, they will be recreated on the next update. */ 
         for( let i=0; i<this.NumPlanes; ++i )
           if( !this.Planes[ i ].destroyed )
             this.DeletePlane( i, true );

         this.Drawing.Update();
      }
      else if( origin == "BigPlanes" )  // Increase plane size
      {
         this.PlaneScale *= 1.5;
         this.AdjustPlanesScale( 1.5 );
      }
      else if( origin == "SmallPlanes" )  // Decrease plane size
      {
         this.PlaneScale /= 1.5;
         this.AdjustPlanesScale( 1.0 / 1.5 );
      }
      else if( origin == "ClosePlaneInfo" )
      {
         // Unselect the plane and close its information popup dialog.
         this.UnselectPlane();
         this.Drawing.Update();
      }
   }
   /* Process mouse clicks on plane icons, implemented as an Action with
      the PlaneSelection label attached to an icon and activated on a 
      mouse click. 
   */
   else if( format == "CustomEvent" )
   {
      let event_label = message_obj.GetSResource( "EventLabel" );   /* String */
      
      /* Plane icon was selected */
      if( event_label == "PlaneSelection" )
      {
         // Get plane index (int).
         let plane_index =
           Math.trunc( message_obj.GetDResource( "Object/PlaneIndex" ) );
         this.SelectPlane( plane_index );
         this.Drawing.Update(); 
      }
   }
}

//////////////////////////////////////////////////////////////////////////
GlgAirCombat.prototype.AdjustPlanesScale = function( /* double */ factor )
{
   const MAX_PLANE_SCALE = 3.375;
   let scale;   /* double */
   
   for( let i=0; i<this.NumPlanes; ++i )
   {
      let plane = this.Planes[ i ];

      if( !plane.destroyed )
      {
         scale = plane.glg_object.GetDResource( "Scale" );
         scale *= factor;        
         if( scale > MAX_PLANE_SCALE )
           scale = MAX_PLANE_SCALE;
         
         plane.glg_object.SetDResource( "Scale", scale );
      }
   }
   
   this.Drawing.Update();
}

//////////////////////////////////////////////////////////////////////////
// Generate a location info string by converting +- sign info into the
// N/S, E/W suffixes, and decimal fraction to deg, min, sec.
//////////////////////////////////////////////////////////////////////////
function CreateLocationString( /* double */ value,
                               /* boolean */ return_lat )   /* String */
{
   let deg, min, sec;    /* int */
   let suffix;           /* String */

   if( return_lat )
   {
      if( value < 0.0 )
      {
         value = -value;
         suffix = 'S';
      }
      else
        suffix = 'N';
      
      deg = Math.trunc( value );
      min = Math.trunc( ( ( value - deg ) * 60.0 ) );
      sec = Math.trunc( ( ( value - deg - min / 60.0 ) * 3600.0 ) );
      
      return ( "" + deg + "\u00B0" + padded( min ) + "'" + padded( sec ) +
               '"' + suffix );
   }
   else   // return lon
   {
      if( value < 0.0 )
      {
         value = -value;
         suffix = 'W';
      }
      else if( value >= 360.0 )
      {
         value -= 360.0;
         suffix = 'E';
      }
      else if( value >= 180.0 )
      {
         value = 180.0 - ( value - 180.0 );
         suffix = 'W';
      }
      else
        suffix = 'E';
      
      deg = Math.trunc( value );
      min = Math.trunc( ( ( value - deg ) * 60.0 ) );
      sec = Math.trunc( ( ( value - deg - min / 60.0 ) * 3600.0 ) );
      return ( "" + deg + "\u00B0" + padded( min ) + "'" + padded( sec ) +
               '"' + suffix );
   }
}

//////////////////////////////////////////////////////////////////////////
// Pads the value with 0 if needed to have a constant field width. 
//////////////////////////////////////////////////////////////////////////
function padded( /* int */ value )   /* String */
{
   if( value < 10 )
     return "0" + value;
   else
     return "" + value;
}

//////////////////////////////////////////////////////////////////////////
function IS_MISSILE( /* int */ plane )    /* boolean */
{
   /* Missiles use odd indices, planes use even ones. */
   return ( plane % 2 ) != 0;
}

//////////////////////////////////////////////////////////////////////////
function IS_PLANE( /* int */ plane )      /* boolean */
{
   return !IS_MISSILE( plane );
}

//////////////////////////////////////////////////////////////////////////
// Converts degrees to radians.
//////////////////////////////////////////////////////////////////////////
function RAD( /* double */ angle )   /* double */
{
   return angle / 180.0 * Math.PI;
}

//////////////////////////////////////////////////////////////////////////
// Converts radians to degrees.
//////////////////////////////////////////////////////////////////////////
function DEG( /* double */ angle )   /* double */
{
   return angle / Math.PI * 180.0;
}

//////////////////////////////////////////////////////////////////////////////
// Changes drawing size while maintaining width/height aspect ratio.
//////////////////////////////////////////////////////////////////////////////
GlgAirCombat.prototype.SetDrawingSize = function( next_size )
{
   const ASPECT_RATIO = 800 / 700;
   
   const MIN_WIDTH = 600;
   const MAX_WIDTH = 1000;
   const SCROLLBAR_WIDTH = 15;

   let span = document.body.clientWidth - SCROLLBAR_WIDTH;    

   if( this.SizeIndex == undefined )   // First time: initialize.
   {
      this.SizeIndex = 0;

      if( this.IsMobile )
      {
         /* Mobile devices use fixed device-width: disable Change Drawing Size 
            button. If it's not standalone, it is handled outside of this 
            script.
         */
         if( this.Standalone )
           RemoveElement( null, this.GLG_div_name + "_change_size" );
      }
      else   /* Desktop */
      {      
         const small_sizes  = [ 1, 1.5,  2.,   2.5 ];
         const medium_sizes = [ 1, 0.75, 1.25, 1.5 ];
         const large_sizes  = [ 1, 0.6,  1.25, 1.5 ];
         
         if( span < 600 )
           this.SetDrawingSize.size_array = small_sizes;
         else if( span < 800 )
           this.SetDrawingSize.size_array = medium_sizes;
         else
           this.SetDrawingSize.size_array = large_sizes;
         
         this.SetDrawingSize.num_sizes = this.SetDrawingSize.size_array.length;
         
         /* Handle browser zooming. */
         this.ResizeListener = ()=>this.SetDrawingSize( false );
         window.addEventListener( "resize", this.ResizeListener );
      }
   }
   else if( next_size )
   {
      ++this.SizeIndex;
      this.SizeIndex %= this.SetDrawingSize.num_sizes;
   }
    
   let drawing_area = document.getElementById( this.GLG_div_name );
   if( this.IsMobile )
   {
      /* Mobile devices use constant device-width, adjust only the height 
         of the drawing to keep the aspect ratio.
      */
      drawing_area.style.height =
        "" + Math.trunc( drawing_area.clientWidth / ASPECT_RATIO ) + "px";
   }
   else   /* Desktop */
   {
      let start_width;
      if( span < MIN_WIDTH )
        start_width = MIN_WIDTH;
      else if( span > MAX_WIDTH )
        start_width = MAX_WIDTH;
      else
        start_width = span;

      let size_coeff = this.SetDrawingSize.size_array[ this.SizeIndex ];
      let width = Math.trunc( Math.max( start_width * size_coeff, MIN_WIDTH ) );
      drawing_area.style.width = "" + width + "px";
      drawing_area.style.height = 
        "" + Math.trunc( width / ASPECT_RATIO ) + "px";
   }

   // Adjust canvas resolution for mobile devices and browser zoom state.
   if( !next_size )
     this.SetCanvasResolution();
}

//////////////////////////////////////////////////////////////////////////////
// Increases canvas resolution for mobile devices with HiDPI displays and for
// browser zooming. Sets CoordScale global variable.
//////////////////////////////////////////////////////////////////////////////
GlgAirCombat.prototype.SetCanvasResolution = function()
{
   let TextScale, PixelOffsetScale, ScreenCoordScale, NativeWidgetTextScale;

   if( this.IsMobile )
   {
      /* CoordScale parameter defines canvas coordinate scaling.
         Values greater than 1 increase canvas resolution and result in 
         sharper rendering. On mobile devices with devicePixelRatio > 1,
         the value of devicePixelRatio may be used for very crisp rendering
         with very thin lines. For pixel ration greater than 2, limit 
         CoordScale to 2 to draw thicker lines.

         CanvasScale > 1 makes text smaller. The TextScale parameter defines
         the text scaling factor used to increase text size.

         The ScreenCoordScale parameter specifies a scaling factor for fixed
         scale viewports that use screen coordinates. If the size of the fixed
         scale viewport (such as a height of a toolbar) is controlled by a pixel
         offset transformation, ScreenCoordScale may be set to the same value as
         PixelOffsetScale to scale the content of the viewport proportionally
         to its height increase.

         The NativeWidgetTextScale parameter defines the scaling factor that is
         used to scale down text in native widgets (such as native buttons, 
         toggles, etc.) to match the scale of the drawing.
      */
      if( window.devicePixelRatio > 2. )
        this.CoordScale = 2.;
      else
        this.CoordScale = window.devicePixelRatio;

      TextScale = 1.7;
      PixelOffsetScale = TextScale;
      ScreenCoordScale = PixelOffsetScale;
      NativeWidgetTextScale = 0.6;
   }
   else   // Desktop
   {
      /* Change canvas resolution to match browser zoom state. */
      this.CoordScale = window.devicePixelRatio;      
      TextScale = window.devicePixelRatio;
      PixelOffsetScale = window.devicePixelRatio;
      ScreenCoordScale = window.devicePixelRatio;
      NativeWidgetTextScale = 1.;    // Don't change native text size.
   }

   GLG.SetCanvasScale( this.CoordScale, TextScale, NativeWidgetTextScale,
                       PixelOffsetScale, ScreenCoordScale );
}

//////////////////////////////////////////////////////////////////////////
function RemoveElement( parent_id, child_id )
{
   let element;
   if( parent_id == null )
     // No parent was provided: remove from document.
     element = document.getElementById( child_id );
   else
   {
      // Parent ID was provided: remove from parent.
      let parent = document.getElementById( parent_id );
      if( parent == null )
        return;
      element = parent.querySelector( "#" + child_id );
   }
   
   if( element != null )
     element.parentNode.removeChild( element );
}

