////////////////////////////////////////////////////////////////////////////// 
// Script entry point: Starts air traffic demo by loading its drawing.
////////////////////////////////////////////////////////////////////////////// 
GlgAirTraffic.prototype.Start = function()
{
   Debug( "Starting: " + this.GLG_div_name );

   this.Active = true;
   
   // Set initial size of the drawing.
   this.SetDrawingSize( false );

   // Show touch action selector for mobile devices.
   if( this.IsMobile )
     ShowElement( this.GLG_div_name + "_touch_action", true );

   /* Load misc. assets such as GLG scrollbars. When assets are loaded, 
      LoadDrawing callback is invoked that loads a GLG drawing defined by
      DrawingName variable.
   */
   this.LoadAssets( ()=>this.LoadDrawing(), null );
}

////////////////////////////////////////////////////////////////////////////// 
// Script entry point: Performs cleanup.
////////////////////////////////////////////////////////////////////////////// 
GlgAirTraffic.prototype.Cleanup = function()
{
   Debug( "Cleanup for: " + this.GLG_div_name );
   
   this.Active = false;    // Ignore any pending updates and callbacks.

   if( this.Drawing )
     this.Drawing.ResetHierarchy();   // Undisplay GLG drawing.

   if( this.ResizeListener )
     window.removeEventListener( "resize", this.ResizeListener );
}

////////////////////////////////////////////////////////////////////////////// 
// Load a GLG drawing from a file.
////////////////////////////////////////////////////////////////////////////// 
GlgAirTraffic.prototype.LoadDrawing = function()
{
   Debug( "LoadDrawing for: " + this.GLG_div_name );
   
   if( !this.Active )
     return;

   /* Load a drawing from the airtraffic.g file. 
      The LoadCB callback will be invoked when the drawing has been loaded.

      Using "bind( this )" as a shorter way to provide "this" compared with 
      using lambda: with bind, we do not need to specify parameter list that
      we would need to provide for lambda.
   */
   let drawing_file = ( this.IsMobile ? "airtraffic2.g" : "airtraffic.g" );   

   GLG.LoadWidgetFromURL( this.GetFullName( drawing_file ), null,
                          this.LoadCB.bind( this ), /*user data*/ null,
                          /*abort test function*/ ()=>!this.Active );
}

////////////////////////////////////////////////////////////////////////////// 
GlgAirTraffic.prototype.GetFullName = function( drawing_name )
{
   if( this.GlgPath == null )
     return drawing_name;

   return this.GlgPath + "/" + drawing_name;
}

//////////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.LoadCB =
  function( /*GlgObject*/ drawing, /*Object*/ user_data, /*String*/ path )
{
   Debug( "LoadCB for: " + this.GLG_div_name );

   if( !this.Active )
     return;

   if( drawing == null )
   {
      AppAlert( "Can't load drawing, check console message for details." );
      return;
   }
   
   if( !document.getElementById( this.GLG_div_name ) )
   {
      Debug( "Can't find " + this.GLG_div_name +
             " div: it may have been removed from the document." );
      return;
   }
    
   // Disable spinning loader.   
   RemoveElement( this.GLG_div_name, "loader_container" );
    
   // Define the element in the HTML page to display the drawing.
   drawing.SetParentElement( this.GLG_div_name );
    
   // Disable viewport border to use the border of the glg_area.
   if( this.Standalone )
     drawing.SetDResource( "LineWidth", 0 );
    
   this.StartAirTrafficDemo( drawing );
}

//////////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.StartAirTrafficDemo = function( drawing )
{
   this.Drawing = drawing;

   this.InitDrawing();
   this.InitIcons();

   this.Drawing.SetupHierarchy();

   this.AddIcons();    // After the hierarchy setup.
   this.Drawing.Update();

   /* After the GIS object has been drawn the first time, we can switch
      to using asynchronous requests for loading GIS images if requested
      by the USE_REQUEST setting.
   */
   this.UseRequest = USE_REQUEST;
   if( this.UseRequest )
   {
      this.gis_request_observer =
        GLG.CreateGlgGISRequestObserver( this.RequestUpdate.bind( this ),
                                         this.RequestAdjustment.bind( this ),
                                         this.AdjustRequest.bind( this ) );

      this.GISObject.SetScrollbarObserver( null, this.gis_request_observer );
   }

   // Start animation.
   setTimeout( ()=>this.UpdatePlanes(), UPDATE_INTERVAL );

   // Zoom to the Florida area after a few seconds to show details.        
   setTimeout( ()=>this.ZoomToFlorida(), FLORIDA_ZOOM_DELAY1 );
}

//////////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.InitDrawing = function()
{
   // Show mouse over tooltips right away.
   this.Drawing.SetDResource( "$config/GlgMouseTooltipTimeout", 0.05 );

   this.Map = this.Drawing.GetResourceObject( "Map" );   
   this.FlightInfoPopup = this.Map.GetResourceObject( "FlightInfoPopup" );
   this.GISObject = this.Map.GetResourceObject( "GISObject" );

   if( SuppliedMapServerURL != null )
     // Override the URL defined in the drawing.
     this.GISObject.SetSResource( "GISMapServerURL", SuppliedMapServerURL );

   // Query and store GIS object's Extent and Center.
   this.InitExtent = this.GISObject.GetGResource( "GISExtent" );
   this.InitCenter = this.GISObject.GetGResource( "GISCenter" );

   // Query and store the GIS projection (ORTHOGRAPHIC or RECTANGULAR)
   this.MapProjection =
     Math.trunc( this.GISObject.GetDResource( "GISProjection" ) );

   /* Set the GIS Zoom mode. It was set and saved with the drawing, 
      but do it again programmatically just in case.
   */
   this.Map.SetZoomMode( null, this.GISObject, null,
                         GLG.GlgZoomMode.GIS_ZOOM_MODE );

   /* Get ID of the GIS Object's GISArray, which holds all icons displayed
      on top of the map in lat/lon coordinates.
   */
   this.GISArray = this.GISObject.GetResourceObject( "GISArray" );

   // Make popup dialog and distance popup invisible.
   this.FlightInfoPopup.SetDResource( "Visibility", 0.0 );
   this.Map.SetDResource( "DistancePopup/Visibility", 0.0 );

   /* Disable the color button: this functionality is not supported with
      the remote map sever setup.
   */
   this.Drawing.SetDResource( "ToggleColor/HandlerDisabled", 1.0 );	  
   
   /* Add Input and Trace callbacks used to handle user interaction. */
   this.Drawing.AddListener( GLG.GlgCallbackType.INPUT_CB,
                             this.InputCallback.bind( this ) );
   this.Drawing.AddListener( GLG.GlgCallbackType.TRACE_CB,
                             this.TraceCallback.bind( this ) );
}

//////////////////////////////////////////////////////////////////////////
// Initializes icons in the drawing
//////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.InitIcons = function()
{
   let icon_name;

   // Get the palette containing templates for plane and node icons.
   let palette = this.Drawing.GetResourceObject( "Palette" );

   // Delete it from the drawing
   this.Drawing.DeleteThisObject( palette );

   /* Get node and plane templates from the palette. A few sets of templates
      with different level of details are used, depending on the zoom level.
      Palette aproach is used to implement icon types instead of 
      subdrawings, since icons are dynamically added/deleted from the 
      drawing any way to show only the icons visible in the zoomed area.
   */
   for( let i=0; i < NUM_PLANE_TYPES; ++i )
   {
      icon_name = "Plane" + i;
      this.PlaneTemplate[ i ] = palette.GetResourceObject( icon_name );

      // Turn labels on initially. 
      if( this.PlaneTemplate[ i ].GetResourceObject( "Label" ) != null )
        this.PlaneTemplate[ i ].SetDResource( "Label/Visibility", 1.0 );
   }

   this.TrajectoryTemplate = palette.GetResourceObject( "Trajectory" );
   this.NumTrajectoryPoints =
     Math.trunc( this.TrajectoryTemplate.GetDResource( "Factor" ) );

   /* Small filled circles don't look nice in a native renderer.
      Add EDGE to make them look nicer.   
   */
   this.TrajectoryTemplate.SetDResource( "Marker/MarkerType", 
                                         ( GLG.GlgMarkerType.FILLED_CIRCLE |
                                           GLG.GlgMarkerType.CIRCLE ) );
   
   for( let i=0; i < NUM_NODE_TYPES; ++i )
   {
      icon_name = "Node" + i;
      this.NodeTemplate[ i ] = palette.GetResourceObject( icon_name );
   }

   // Create and initialize plane structures used for simulation.
   this.PlaneArray = new Array( this.NumPlanes );
   for( let i=0; i < this.NumPlanes; ++i )
   {
      let plane = new PlaneData();
      plane.name = i.toString();
      plane.color_index = NORMAL;
      this.PlaneArray[ i ] = plane;
      
      this.StartPlane( this.PlaneArray[ i ], true );
   }

   // Create groups to hold nodes and planes.
   this.NodeGroup =
     GLG.CreateObject( GLG.GlgObjectType.ARRAY,
                       GLG.GlgContainerType.GLG_OBJECT, 0, 0, null );
   this.NodeGroup.SetSResource( "Name", "NodeGroup" );

   this.TrajectoryGroup =
     GLG.CreateObject( GLG.GlgObjectType.ARRAY,
                       GLG.GlgContainerType.GLG_OBJECT, 0, 0, null );
   this.TrajectoryGroup.SetSResource( "Name", "TrajectoryGroup" );

   this.PlaneGroup =
     GLG.CreateObject( GLG.GlgObjectType.ARRAY,
                       GLG.GlgContainerType.GLG_OBJECT, 0, 0, null );
   this.PlaneGroup.SetSResource( "Name", "PlaneGroup" );

   /* Add all icons to the GIS object, so that the icon's position may be
      defined in lat/lon. The GIS object handles all details of the 
      GIS coordinate conversion.
   */
   this.GISObject.AddObjectToBottom( this.NodeGroup );
   this.GISObject.AddObjectToBottom( this.PlaneGroup );
   this.GISObject.AddObjectToBottom( this.TrajectoryGroup );

   /* Create groups to keep pooled object icons. Only the icons visible in the
      current view are displayed. Any not used icons are stored in the icon 
      pool.
   */
   for( let i=0; i<NUM_NODE_TYPES; ++i )
     this.NodePool[i] =
       GLG.CreateObject( GLG.GlgObjectType.ARRAY,
                         GLG.GlgContainerType.GLG_OBJECT, 0, 0, null );
   
   for( let i=0; i<NUM_PLANE_TYPES; ++i )
     this.PlanePool[i] =
       GLG.CreateObject( GLG.GlgObjectType.ARRAY,
                         GLG.GlgContainerType.GLG_OBJECT, 0, 0, null );
      
   this.TrajectoryPool =
     GLG.CreateObject( GLG.GlgObjectType.ARRAY,
                       GLG.GlgContainerType.GLG_OBJECT, 0, 0, null );

   this.SetPlaneSize();

   /* The demo starts with the whole word view, then zooms to the Florida area
      in a few seconds to show more details. Set initial parameters
      for the whole world view.
      */
   this.SetupZoomLayers( null );

   this.RequestNewMap( false, false );

   // Store objects used to display lat/lon on mouse move.
   this.PositionArea = this.Drawing.GetResourceObject( "PositionArea" );
   this.PositionObject =
     this.Drawing.GetResourceObject( "PositionLabel/String" );
   this.PositionObject.SetSResource( null, "" );

   // Set Florida zooming message to OFF initially.
   this.Drawing.SetDResource( "Map/FloridaZoomingMessage/Visibility", 0.0 );
}

//////////////////////////////////////////////////////////////////////////
// Invoked after the hierarchy setup.
//////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.AddIcons = function()
{
   // Add visible icons to the drawing and handle zoom level.
   this.UpdateObjectsOnMap( false );

   this.SetMapLoading( true, "Loading map..." );
}

//////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.SetMapLoading = function( show, message )
{
   if( show )
   {
      this.SetStatus( message );
      this.Map.SetSResource( "LoadingMessage/String", message );
      this.Map.SetDResource( "LoadingMessage/Visibility", 1 );
      this.MapLoaded = false;
   }
   else
   {
      this.SetStatus( "" );
      this.Map.SetDResource( "LoadingMessage/Visibility", 0 );
      this.MapLoaded = true;
   }
}

//////////////////////////////////////////////////////////////////////////
// Makes sure the icons visible at the current zoom level are added
// to the drawing, keeping the rest of the icons in the object pool.
// Also handles the use of more detailed icons depending on the zoom 
// level.
// The object pools are used to handle huge number of icons efficiently.
// For a smaller number of icons, icons may always be kept in the drawing
// and this function is not needed.
//////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.UpdateObjectsOnMap = function( using_request )
{
   // Delete old nodes and planes.
   this.DeleteNodes();
   this.DeletePlanes();
   
   // When using a request, SetupZoomLayers was invoked from AdjustRequest.
   if( !using_request )
   {
      this.SetupZoomLayers( null );
      if( this.NewFlags != 0 )
        this.RequestNewMap( false, false );  // Flush changes into the map.
   }

   this.SetupZoomIcons();

   /* Update the GIS object with new extent but don't draw it yet:
      we want to position icons on the map first.
   */
   this.Map.SetupHierarchy();
   
   for( let i=0; i < this.NumNodes; ++i )      // Position nodes
     this.PositionNode( this.NodeArray[ i ], i );

   /* Add trajectories before positioning planes to setup trajectories' 
      history.
   */
   this.GISObject.AddObjectToBottom( this.TrajectoryGroup );
   
   for( let i=0; i < this.NumPlanes; ++i )     // Position planes
     this.PositionPlane( this.PlaneArray[ i ], i );
   
   // Add new nodes and planes.
   this.GISObject.AddObjectToBottom( this.NodeGroup );
   this.GISObject.AddObjectToBottom( this.PlaneGroup );
   
   this.SelectPlane( this.SelectedPlaneIndex );
}

//////////////////////////////////////////////////////////////////////////
// Set displayed layers depending on zoom level. 
// Also change to rectangular projection for high zoom factors.
//////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.SetupZoomLayers = function( request_data )
{
   let base_layers;      /* String */
   let city_labels;      /* boolean */
   
   if( request_data == null )
     this.FlushMapChanges();
   
   let extent = this.GetExtentDegrees( request_data );   /* GlgPoint */

   if( extent.x < 70.0 && extent.y < 70.0 )
   {
      base_layers = "default_air";
      city_labels = true;     // Use city names instead of airport labels.
   }
   else    // Whole world view 
   {
      base_layers = "default_gis,grid70,outline";
      city_labels = false;   // Use airport labels instead of all city names.
   }
   
   // Adjust if changed.
   if( base_layers != this.BaseLayers || city_labels != this.CityLabels )
   {
      let layers = this.CreateLayerString( base_layers, city_labels );
      
      if( request_data != null )
      {
         /* Adjust layers in request data. */
         request_data.layers = layers;
      }
      else
      {
         this.NewLayers = layers;
         this.NewFlags |= GLG.GlgGISRequestFlags.GIS_REQUEST_LAYERS;
      }
      
      this.NewBaseLayers = base_layers;
      this.NewCityLabels = city_labels;
      this.NewCityLabelsFlag = true;
   }

   if( !ORTHO_ONLY )
     this.ChangeProjection( request_data );
}

////////////////////////////////////////////////////////////////////////////////
// Add map layers: airport/city labels and states.
////////////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.CreateLayerString = function( base_layers, city_labels )
{
   let layers = base_layers;
   
   if( city_labels )
     layers += ",us_cities";
   else
     layers += ",-us_cities";
   
   if( this.StateDisplay )   // Add states layer if it is on.
     // Enable states regardless of the default.
     layers += ",states_dcw";
   else
     // Disable state outline display.
     layers += ",-states_dcw";
   
   return layers;
}

////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.SetGISLayers = function()
{
   this.NewLayers = this.CreateLayerString( this.BaseLayers, this.CityLabels );
   this.NewFlags = GLG.GlgGISRequestFlags.GIS_REQUEST_LAYERS;
   
   this.RequestNewMap( false, true );
}

