   /* Controls asynchronous map requests.
      Don't change: must always be false for initial map loading, will be set
      to the value of USE_REQUEST after that.
      Change USE_REQUEST above to use asynchronous map requests.
   */
   this.UseRequest = false;

   this.MapLoaded = false;                 /* boolean */
   this.InitialLoad = true;                /* boolean */
   this.PerformUpdates = true;             /* boolean */
   this.PanMode = false;                   /* boolean */
