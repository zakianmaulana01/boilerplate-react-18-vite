// Set node types depending on the zoom level.
//////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.SetupZoomIcons = function()
{
   let extent = this.GetExtentDegrees( null );   /* GlgPoint */

   if( extent.x < 20.0 && extent.y < 20.0 )
   {
      // High Zoom: use the most detailed icon.
      this.NodeType = 1;
      this.PlaneType = 2;         // Most detailed icon
      this.HasAngle = true;       // Most detailed plane icons show angle
      this.HasElevation = true;   // Most detailed plane icons show elevation
   }
   else if( extent.x < 70.0 && extent.y < 70.0 )
   {
      // Zoom: use detailed icon 1 
      this.NodeType = 1;
      this.PlaneType = 1;         // Detailed icon 
      this.HasAngle = true;       // Detailed plane icons show angle 
      this.HasElevation = false;  
   }
   else
   {
      // Whole world view 
      this.NodeType = 1;          // City icons are always visible 
      this.PlaneType = 0;         // Simple icon 
      this.HasAngle = false;
      this.HasElevation = false;
   }
   
   // Airport labels should be visible only when city labels are off.
   this.NodeTemplate[1].SetDResource( "Label/Visibility",
                                      this.CityLabels ? 0.0 : 1.0 );
}

//////////////////////////////////////////////////////////////////////////
// Change projection to rectangular for zoomed views, and back to 
// orthographics for high-level views.
//////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.ChangeProjection = function( request_data )
{
   let projection = this.GetMapProjection( request_data );

   let extent = this.GetExtentDegrees( request_data );   /* GlgPoint */
   if( extent.x < 30.0 && extent.y < 30.0 )
   {
      if( projection == GLG.GlgProjectionType.RECTANGULAR_PROJECTION )
        return;    // Already rectangular, no change.
      
      // Change to the rectangular projection, extent is already in degrees.
      projection = GLG.GlgProjectionType.RECTANGULAR_PROJECTION;
   }
   else
   {
      if( projection == GLG.GlgProjectionType.ORTHOGRAPHIC_PROJECTION )
        return;    // Already ortho, no change
      
      // Change to the orthographic projection
      projection = GLG.GlgProjectionType.ORTHOGRAPHIC_PROJECTION;
      
      // Change extent to meters
      extent.x = extent.x / 90.0 * GLG.EQUATOR_RADIUS;
      extent.y = extent.y / 90.0 * GLG.POLAR_RADIUS;
   }
   
   if( request_data != null )
   {
      // Adjust projection and extent in request data.
      request_data.projection = projection;
      request_data.extent = extent;  // Set extent in degrees
   }
   else
   {
      this.NewMapProjection = projection;
      this.NewExtent = extent;  // Set extent in degrees
      this.NewFlags |= ( GLG.GlgGISRequestFlags.GIS_REQUEST_PROJECTION | 
                         GLG.GlgGISRequestFlags.GIS_REQUEST_EXTENT );
   }
}

//////////////////////////////////////////////////////////////////////////
// Handle user interaction.
//////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.InputCallback = function( viewport, message_obj )
{
   if( !this.Active )
     return;

   let origin = message_obj.GetSResource( "Origin" );
   let format = message_obj.GetSResource( "Format" );
   let action = message_obj.GetSResource( "Action" );
   let subaction = message_obj.GetSResource( "SubAction" );

   if( format == "Button" )         // Handle button clicks
   {
      if( action != "Activate" )
        return;
      
      this.PanMode = false;       // Abort Pan mode

      if( this.DistanceMode )           
      {
         this.AbortDistanceMode();  // Abort the distance mode in progress.

         /* Second click on the Distance button: cancel DistanceMode in
            progress.
         */
         if( origin == "Distance" )
           return;
      }

      this.AbortDistanceMode();
      
      if( origin == "CloseDialog" )
      {
         this.Drawing.SetDResource( "SelectionDialog/Visibility", 0.0 );
         this.Drawing.Update();	 
      }
      else if( origin == "Up" )
      {	    
         this.Zoom( 'u', 0.0 );
      }
      else if( origin == "Down" )
      {	    
         this.Zoom( 'd', 0.0 );
      }
      else if( origin == "Left" )
      {	    
         this.Zoom( 'l', 0.0 );
      }
      else if( origin == "Right" )
      {	    
         this.Zoom( 'r', 0.0 );
      }
      else if( origin == "ZoomIn" )
      {
         this.Zoom( 'i', 2.0 );
      }
      else if( origin == "ZoomOut" )
      {
         this.Zoom( 'o', 2.0 );
      }
      else if( origin == "ZoomReset" )
      {	
         this.Zoom( 'n', 0.0 );
      }
      else if( origin == "ZoomTo" )
      {
         this.Zoom( 't', 0.0 );
      }
      else if( origin == "Pan" )
      {	    
         this.Map.SetZoom( null, 'e', 0.0 );  // Abort ZoomTo/Drag mode
         
         this.PanMode = true;
         this.SetStatus( "Click to define a new center." );
         this.Drawing.Update();	 
      }
      else if( origin == "Drag" )
      {
         /* Activate dragging mode. Dragging will start on the mouse click. 
            If no object of interest is selected by the mouse click, 
            dragging will be started by the code in the Trace callback 
            anyway, but only if no object of interest was selected. 
            The "Drag" button demostrates an alternative way to start 
            dragging from a button which starts dragging even if an object 
            of interest is selected by the mouse click.
         */
         this.DraggingFromButton = true;
         this.Zoom( 's', 0.0 );
         this.SetStatus( "Click and drag the map with the mouse." );
         this.Drawing.Update();
      }
      else if( origin == "AirportLabels" )
      {
         this.CityLabels = !this.CityLabels;
         this.SetGISLayers();
      }
      else if( origin == "ToggleStates" )
      {
         this.StateDisplay = !this.StateDisplay;
         this.SetGISLayers();
      }
      else if( origin == "Planes" )
      {
         ToggleResource( this.Map, "PlaneGroup/Visibility" );
         this.Drawing.Update();	 
      }
      else if( origin == "ValueDisplay" )
      {
         if( this.PlaneType == 0 )
         {
            GLG.Bell();
            this.SetStatus( "Zoom in to see plane labels." );
         }
         else
         {
            // Visibility of all labels is constrained, set just one.
            for( let i=1; i<NUM_PLANE_TYPES; ++i )
              ToggleResource( this.PlaneTemplate[ i ], "Label/Visibility" );
         }
         this.Drawing.Update();	 
      }
      else if( origin == "Update" )
      {
         this.PerformUpdates = !this.PerformUpdates;
         this.Drawing.Update();
      }	
      else if( origin == "PlaneSize" )
      {
         // Change plane icon's size.
         if( this.PlaneSize == SMALL_SIZE )
           this.PlaneSize = MEDIUM_SIZE;
         else if( this.PlaneSize == MEDIUM_SIZE )
           this.PlaneSize = BIG_SIZE;
         else // BIG_SIZE
           this.PlaneSize = SMALL_SIZE;	 
         
         this.SetPlaneSize();
         
         this.Drawing.Update();	 
      }
      else if( origin == "CloseFlightInfo" )
      {
         this.SelectPlane( -1 ); // Unselect the plane and erase popup display.
         this.Drawing.Update();	 
      }	
      else if( origin == "Distance" )
      {
         this.DistanceMode = true;
         this.SetStatus( "Click on the map to define distance to measure," +
                         " press the button again or right click to finish." );
         this.Drawing.Update();
      }
   }
   /* Process mouse clicks on plane icons, implemented as an Action with
      the Plane label attached to an icon and activated on a mouse click. 
   */
   else if( format == "CustomEvent" )
   {	
      /* Ignore selection in the Distance or DraggingFromButton mode,
         as well as in the Scroll mode on mobile devices.
      */
       if( this.DistanceMode || this.DraggingFromButton ||
           ( this.ScrollMode && this.IsMobile ) )
        return;

      let event_label = message_obj.GetSResource( "EventLabel" );
         
      if( event_label == "Plane" )  // Plane icon was selected
      {
         /* Map dragging mode is activated either on a mouse click in the 
            trace callback, or with the Drag toolbar button. 
            Abort the dragging mode if an object with custom event was 
            selected and the dragging was activated on a mouse click. 
            This gives custom events a higher priority compared to the 
            dragging mode. If it's a ZoomTo mode 
            activated by a button, don't abort it and ignore object selection.
         */
         let zoom_mode = this.ZoomToMode();
         if( zoom_mode == 0 ||
             ( zoom_mode & GLG.GlgZoomState.PAN_DRAG_STATE ) != 0 )
         {
            if( zoom_mode != 0 )
              this.Map.SetZoom( null, 'e', 0.0 );  /* Abort zoom mode */
            
            // Get plane index 
            let plane_index =
              Math.trunc( message_obj.GetDResource( "Object/DataIndex" ) );
            this.SelectPlane( plane_index );
               
            // Show message in the bottom
            this.SetStatus( this.PlaneArray[ this.SelectedPlaneIndex ].tooltip );
               
            this.DisplayPlaneInfo();   // Display popup
            this.Drawing.Update();
         }
      }
   }
   else if( action == "Zoom" )
   {
      if( subaction == "End" )
      {
         /* Update icon positions after zooming if not using request.
            If using request, it is done when the request is installed.
         */
         if( !this.UseRequest )
           this.UpdateObjectsOnMap( false );
         
         /* Wait for map loading if the request wasn't aborted by an invalid
            ZoomTo rectangle with zero width / height.
         */
         if( this.GISObject.GetGISRequestInfo( null ) != null )
           this.SetMapLoading( true, "Loading map..." );

         this.Drawing.Update();
      }
      else if( subaction == "Abort" )
        this.SetStatus( "" );
   }
   else if( action == "Pan" )
   {
      if( subaction == "Start" )   // Map dragging start
      {
         this.SetStatus( "Drag the map with the mouse." );
      }
      else if( subaction == "Drag" )    // Dragging
      {
         this.DraggingFromButton = false;
         
         /* Update icon positions after zooming if not using request.
            If using request, it is done when the request is installed.
         */
         if( !this.UseRequest )
         {
            this.UpdateObjectsOnMap( false );
            this.SetMapLoading( true, "Loading map..." );
            this.SetStatus( "Dragging the map with the mouse..." );
         }
         else
           this.SetMapLoading( true, "Release the mouse to show the new map." );

         this.Drawing.Update();
      }
      else if( subaction == "ValueChanged" )   // Scrollbars
      {
         /* Update icon positions after scrolling if not using request.
            If using request, it is done when the request is installed.
         */
         if( !this.UseRequest )
           this.UpdateObjectsOnMap( false );

         this.SetMapLoading( true, "Loading map..." );
         this.Drawing.Update();         
      }
      // Dragging ended or aborted (right mouse button, etc.). 
      else if( subaction == "End" || subaction == "Abort" )
      {
         this.DraggingFromButton = false;
         if( this.UseRequest && subaction != "Abort" )
           this.SetMapLoading( true, "Loading map..." );
         else
           // Reset the message when dragging ends.
           this.SetMapLoading( false, "" );

         this.Map.Update();
      }
   }   
}

