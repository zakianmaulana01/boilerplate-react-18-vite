//////////////////////////////////////////////////////////////////////////
// Is used to obtain coordinates of the mouse click. 
//////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.TraceCallback = function( viewport, trace_info )
{      
   if( !this.Active )
     return;

   let event_type = trace_info.event_type;
   let x, y;

   // Use the Map area events only.
   if( !trace_info.viewport.Equals( this.Map ) )
   {
      /* Erase the current postion display when the mouse moves outside 
         of the map.
      */
      switch( event_type )
      {
       case GLG.GlgEventType.MOUSE_MOVED:
         this.PositionObject.SetSResource( null, "" );
         this.PositionArea.Update();
         break;

       default: break;
      }
      return;
   }

   switch( event_type )
   {
    case GLG.GlgEventType.KEY_DOWN:
      if( trace_info.event.keyCode == 27 )      // ESC key
        this.AbortDistanceMode();
      return;

    case GLG.GlgEventType.TOUCH_START:
      if( !this.DraggingFromButton && !this.DistanceMode && !this.ScrollMode )
        return;
      
      GLG.SetTouchMode();        /* Start dragging via touch events. */
      /* Fall through */

    case GLG.GlgEventType.TOUCH_MOVED:
      if( !GLG.GetTouchMode() )
        return;
      /* Fall through */
    case GLG.GlgEventType.MOUSE_PRESSED:
    case GLG.GlgEventType.MOUSE_MOVED:
      x = trace_info.mouse_x * this.CoordScale;
      y = trace_info.mouse_y * this.CoordScale;

      /* COORD_MAPPING_ADJ is added to the cursor coordinates for precise
         pixel mapping.
      */
      x += GLG.COORD_MAPPING_ADJ;
      y += GLG.COORD_MAPPING_ADJ;
      break;

    default: return;
   }
 
   let point_obj;    /* GlgObject */

   switch( event_type )
   {
    case GLG.GlgEventType.TOUCH_START:
    case GLG.GlgEventType.MOUSE_PRESSED:
      if( this.ZoomToMode() != 0 )
        return; // ZoomTo or dragging mode in progress: pass it through.

      if( trace_info.button != 1 )
      {
         this.AbortDistanceMode();
         return;  // Use only the left button clicks.
      }
      
      /* Handle paning: set the new map center to the location of the click. */
      if( this.PanMode )
      {
         this.PanMode = false;
         this.SetStatus( "" );
         
         // Converts X/Y to lat/lon using GIS object's current projection.
         this.point.x = x;
         this.point.y = y;
         this.point.z = 0.0;
         this.GISObject.GISConvert( null, GLG.GlgCoordType.SCREEN_COORD,
                                    /* X/Y to Lat/Lon */ true,
                                    this.point, this.lat_lon );
         
         // Pan the map
         this.NewCenter =
           GLG.CreateGlgPoint( this.lat_lon.x, this.lat_lon.y, 0 );
         this.NewFlags = GLG.GlgGISRequestFlags.GIS_REQUEST_CENTER;
         this.RequestNewMap( true, true );
      }
      else if( this.DistanceMode )
      { 
         /* The world coordinates inside the GIS Object's GISArray are in 
            lat/lon.
         */
         this.point.x = x;
         this.point.y = y;
         this.point.z = 0.0;
         this.GISArray.ScreenToWorld( true, this.point, this.lat_lon );
               
         if( this.DistancePolygon == null )
         {
            this.DistancePolygon =
              GLG.CreateObject( GLG.GlgObjectType.POLYGON, 2,
                                null, null, null );
            this.DistancePolygon.SetGResource( "EdgeColor", 1.0, 1.0, 0.0 );
            this.NumDistancePoints = 1;

            point_obj = this.DistancePolygon.GetElement( 0 );
            point_obj.SetGResourceFromPoint( null, this.lat_lon );
               
            point_obj = this.DistancePolygon.GetElement( 1 );
            point_obj.SetGResourceFromPoint( null, this.lat_lon );
               
            this.GISObject.AddObjectToBottom( this.DistancePolygon );
            this.Map.Update();
         }
         else // Not the first point
         {
            // Set current point to the coord. of the click.
            point_obj =
              this.DistancePolygon.GetElement( this.NumDistancePoints );
            point_obj.SetGResourceFromPoint( null, this.lat_lon );
            ++this.NumDistancePoints;
               
            this.DisplayDistance( this.DistancePolygon );
            
            // Add next point, same coords.
            point_obj = point_obj.CopyObject();
            this.DistancePolygon.AddObjectToBottom( point_obj );
               
            this.Map.Update();
         }
      }
      else 
        // Not a Pan or Distance mode: start dragging the map with the mouse.
        this.Zoom( 's', 0.0 );
      break;
      
    case GLG.GlgEventType.TOUCH_MOVED:
    case GLG.GlgEventType.MOUSE_MOVED:
      this.point.x = x;
      this.point.y = y;
      this.point.z = 0;
         
      // The world coordinates inside the GIS Object's GISArray are in lat/lon.
      this.GISArray.ScreenToWorld( true, this.point, this.lat_lon );

      if( this.DistanceMode && this.DistancePolygon != null )
      {
         point_obj = this.DistancePolygon.GetElement( this.NumDistancePoints );
         point_obj.SetGResourceFromPoint( null, this.lat_lon );
         this.DisplayDistance( this.DistancePolygon );
         this.Map.Update();
      }

      // Display lat/lon of a point under the mouse.
      this.PositionObject.SetSResource( null,
                                        CreateLocationString( this.lat_lon ) );
      this.PositionArea.Update();
      break;
         
    default: return;
   }
}

//////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.AbortDistanceMode = function()
{
   if( this.DistanceMode )
   {
      if( this.DistancePolygon != null )   // Delete distance polygon
      {
         if( this.GISObject.ContainsObject( this.DistancePolygon ) )
           this.GISObject.DeleteThisObject( this.DistancePolygon );
            
         this.DistancePolygon = null;
      }
      this.Map.SetDResource( "DistancePopup/Visibility", 0.0 );
      this.SetStatus( "" );
      this.DistanceMode = false;
   }
}

//////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.DisplayDistance = function( /* GlgObject */ polygon )
{
   // Popup distance display
   this.Map.SetDResource( "DistancePopup/Visibility", 1.0 );
      
   // Last point is for dragging, not set yet - don't include.
   let size = polygon.GetSize();
   if( size < 2 )
     return;
      
   let distance = 0.0;
   let point = null;     /* GlgObject */
   for( let i=0; i<size; ++i )
   {
      let last_point = point;    /* GlgObject */
      point = polygon.GetElement( i );
            
      if( last_point != null )
        // Nautical mile = 1842m 
        distance += GetGlobeDistance( point, last_point ) / 1842.0;
   }
      
   this.Map.SetDResource( "DistancePopup/Distance", distance );
}
   
//////////////////////////////////////////////////////////////////////////
// Returns the length (in meters) of the shortest arc along the earth 
// surface connecting the two points.
//////////////////////////////////////////////////////////////////////////
function GetGlobeDistance( /* GlgObject */ point1_obj,
                           /* GlgObject */ point2_obj )
{
   let lat_lon1, lat_lon2, globe_point1, globe_point2;   /* GlgPoint */
   
   lat_lon1 = point1_obj.GetGResource( null );
   lat_lon2 = point2_obj.GetGResource( null );
      
   // XYZ of the first point, in meters.
   globe_point1 = GetPointXYZ( lat_lon1 );
      
   // XYZ of the second point, in meters.
   globe_point2 = GetPointXYZ( lat_lon2 );
      
   let dx = globe_point1.x - globe_point2.x;
   let dy = globe_point1.y - globe_point2.y;
   let dz = globe_point1.z - globe_point2.z;

   // Length of a straight line between the points.
   let straight_dist = Math.sqrt( dx * dx + dy * dy + dz * dz );
   
   // Use the average value.
   let globe_radius = ( GLG.EQUATOR_RADIUS + GLG.POLAR_RADIUS ) / 2.0;

   // The length of the shortest connecting arc along the earth surface. 
   let arc_dist = 2.0 * globe_radius *
     Math.asin( straight_dist / ( 2.0 * globe_radius ) );
   
   return arc_dist;
}

//////////////////////////////////////////////////////////////////////////
function GetPointXYZ( /* GlgPoint */ lat_lon )     /* GlgPoint */
{
   let
     angle_x,
     angle_y;
   
   let xyz =  GLG.CreateGlgPoint( 0, 0, 0 );   /* GlgPoint */

   /* For simplicity, place the origin at the intersection of the x axis 
      with the globe surface. */
   
   angle_x = DegToRad( lat_lon.x );
   angle_y = DegToRad( lat_lon.y );
      
   xyz.x = GLG.EQUATOR_RADIUS * Math.cos( angle_x ) * Math.cos( angle_y );
   xyz.y = GLG.EQUATOR_RADIUS * Math.sin( angle_y );
   xyz.z = GLG.EQUATOR_RADIUS * Math.sin( angle_x ) * Math.cos( angle_y );

   return xyz;
}

//////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.Zoom = function( type, value )
{
   switch( type )
   {
    default:
      if( this.UseRequest )
      {
         /* Prepare new map image in a separate thread. The requested image
            will be installed using InstallGISRequest when it is ready.
            CheckScrollLimits will be invoked from AdjustRequest, and
            UpdateObjectsOnMap will be invoked when the request is installed.
         */

         this.Map.RequestGISZoom( null, type, value,
                                  this.gis_request_observer );
         this.RequestType = type;
      }
      else
      {
         this.Map.SetZoom( null, type, value );
         this.RequestType = type;
         
         this.CheckScrollLimits( null );
         this.UpdateObjectsOnMap( false );
      }

      this.SetMapLoading( true, "Loading map..." );
      this.Drawing.Update();
      break;

    case 'n':
      // Reset map to the initial extent.
      this.NewMapProjection = GLG.GlgProjectionType.ORTHOGRAPHIC_PROJECTION;
      this.NewCenter = GLG.CopyGlgPoint( this.InitCenter );
      this.NewExtent = GLG.CopyGlgPoint( this.InitExtent );
      this.NewFlags = ( GLG.GlgGISRequestFlags.GIS_REQUEST_PROJECTION | 
                        GLG.GlgGISRequestFlags.GIS_REQUEST_CENTER | 
                        GLG.GlgGISRequestFlags.GIS_REQUEST_EXTENT );
      
      this.RequestNewMap( true, true );
      break;
      
    case 't':
      // Start ZoomTo
      if( this.UseRequest )
        this.Map.RequestGISZoom( null, 't', 0.0, this.gis_request_observer );
      else
        this.Map.SetZoom( null, 't', 0.0 );
      
      this.SetStatus( "Define a rectangular area to zoom to." );
      this.Drawing.Update();	 
      break;
      
    case 's':
      // Start dragging the map with the mouse
      if( this.UseRequest )
        this.Map.RequestGISZoom( null, 's', 0.0, this.gis_request_observer );
      else
        this.Map.SetZoom( null, 's', 0.0 );
      
      this.Drawing.Update();	 
      break;
   }
}

//////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.RequestNewMap =
  function( /* boolean */ update_objects, /* boolean */ call_update )
{
   if( this.NewFlags == 0 )
     return;   // No change
   
   if( this.UseRequest )
   {
      /* Prepare new map image in a separate thread. The requested image
         will be installed using InstallGISRequest when it is ready.
         StoreNewValues and UpdateObjectsOnMap will be invoked when the 
         request is installed.
      */
      this.GISObject.RequestGISMap( null, this.NewExtent.x, this.NewExtent.y, 
                                    this.NewCenter.x, this.NewCenter.y, 0,
                                    this.NewMapProjection, this.NewLayers,
                                    this.NewFlags, this.gis_request_observer );
      this.NewFlags = 0;
   }
   else   /* Set GIS object parameters directly and let it load the map. */
   {
      if( ( this.NewFlags & GLG.GlgGISRequestFlags.GIS_REQUEST_PROJECTION ) != 0 )
        this.GISObject.SetDResource( "GISProjection", this.NewMapProjection );
      if( ( this.NewFlags & GLG.GlgGISRequestFlags.GIS_REQUEST_EXTENT ) != 0 )
        this.GISObject.SetGResourceFromPoint( "GISExtent", this.NewExtent );
      if( ( this.NewFlags & GLG.GlgGISRequestFlags.GIS_REQUEST_CENTER ) != 0 )
        this.GISObject.SetGResourceFromPoint( "GISCenter", this.NewCenter );
      if( ( this.NewFlags & GLG.GlgGISRequestFlags.GIS_REQUEST_LAYERS ) != 0 )
        this.GISObject.SetSResource( "GISLayers", this.NewLayers );
      this.NewFlags = 0;
      
      this.StoreNewValues();

      if( update_objects )
        this.UpdateObjectsOnMap( false );
      if( call_update )
        this.Drawing.Update();
   }

   this.SetMapLoading( true, "Loading map..." );
}
  
//////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.StoreNewValues = function()
{
   if( this.NewBaseLayers != null )
   {
      this.BaseLayers = this.NewBaseLayers;
      this.NewBaseLayers = null;
   }
   
   if( this.NewCityLabelsFlag )
   {
      this.CityLabels = this.NewCityLabels;
      this.NewCityLabelsFlag = false;
   }
   
   // Store a possibly changed projection.
   this.MapProjection =
     Math.trunc( this.GISObject.GetDResource( "GISProjection" ) );
}

//////////////////////////////////////////////////////////////////////////
// For rectangular projection on the detailed map, make sure the map 
// does not scroll beyond the poles in the vertical direction.
//////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.CheckScrollLimits = function( request_data )
{
   let extent, center;       /* GlgPoint */
   let min_y, max_y;         /* double */
   let adjust_x, adjust_y;   /* boolean */
      
   /* Since CheckScrollLimits may be invoked with null request data in 
      non-request mode (USE_REQUEST=false), we can't use request_data.flags
      to determine request type. Instead, the RequestType variable 
      is used to indicate request type for both modes.
   */
   let type = this.RequestType;
   this.RequestType = null;

   switch( type )
   {
    case 'u':  // Scroll up
    case 'd':  // Scroll down
    case 'l':  // Scroll left
    case 'r':  // Scroll right
      break; // Adjust only for scroll types.
    default: return;   // Don't adjust for other zoom types.
   }
      
   let projection = this.GetMapProjection( request_data );
   if( projection == GLG.GlgProjectionType.ORTHOGRAPHIC_PROJECTION )
     return;   // Allow unlimited scrolling for ortho.
      
   extent = this.GetMapExtent( request_data );
   center = this.GetMapCenter( request_data );

   min_y = center.y - extent.y / 2.0;
   max_y = center.y + extent.y / 2.0;
   
   /* Check and adjust center lat to make sure the map does not scroll 
      beyond the poles in the vertical direction. 
   */
   adjust_y = true;
   if( extent.y >= 180.0 )
     center.y = 0.0;
   else if( min_y < -90.0 )
     center.y = -90.0 + extent.y / 2.0;
   else if( max_y > 90.0 )
     center.y = 90.0 - extent.y / 2.0;
   else
     adjust_y = false;
   
   /* Allow scrolling to +-180 in horizontal direction, to match the
      range of the horizontal scrollbar.
   */
   adjust_x = true;
   if( center.x < -180.0 )
     center.x = -180.0;
   else if( center.x > 180.0 )
     center.x = 180.0;
   else
     adjust_x = false;
   
   /* Set adjusted center. If called from AdjustRequest, it's already
      modified in-place in request_data - do nothing.
   */
   if( request_data == null && ( adjust_x != 0 || adjust_y != 0 ) )
     this.GISObject.SetGResourceFromPoint( "GISCenter", center );
}

