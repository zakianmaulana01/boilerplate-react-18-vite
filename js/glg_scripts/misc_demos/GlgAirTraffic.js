// Zoom to the Florida area after a few seconds to show details.
//////////////////////////////////////////////////////////////////////////
GlgAirTraffic.prototype.ZoomToFlorida = function()
{      
   if( !this.Active )
     return;
   
   this.AbortDistanceMode();

   // Show zoom message.
   this.Drawing.SetDResource( "Map/FloridaZoomingMessage/Visibility", 1.0 );
   this.Drawing.Update();

   /* GISExtent is in lon/lat for the rectangular GIS projection,
      and in meters for the orthographic projection.
      To find proper values, zoom the map in the GlgBuilder and 
      copy the GISExtent values. */
   
   // Zoom to the Florida boundaries on detailed map.
   this.NewExtent = GLG.CreateGlgPoint( 1169530.0, 1169530.0, 0.0 );
   this.NewCenter = GLG.CreateGlgPoint( -82.8239, 28.9382, 0.0 );
   this.NewFlags = ( GLG.GlgGISRequestFlags.GIS_REQUEST_EXTENT |
                     GLG.GlgGISRequestFlags.GIS_REQUEST_CENTER );

   this.RequestNewMap( true, false );

   /* Reorder Florida zoom message to the top, in case any objects 
      were added on top of it.
   */
   let florida_message =
     this.Drawing.GetResourceObject( "Map/FloridaZoomingMessage" );
   this.Map.ReorderElement( this.Map.GetIndex( florida_message ),
                            this.Map.GetSize() - 1 );
   this.Drawing.Update();

   // Erase zoom message after a delay.
   setTimeout( ()=>this.ZoomToFloridaEnd(), FLORIDA_ZOOM_DELAY2 );
}

