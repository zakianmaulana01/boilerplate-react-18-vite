//////////////////////////////////////////////////////////////////////////////
// GIS Air Traffic Monitoring Demo: an example of using the GLG Map Server
// integrated in the GLG drawing.
//
// The demo is written in pure HTML5 and JavaScript. The source code of the
// demo uses the GLG Toolkit JavaScript Library supplied by the included
// Glg*.js and GlgToolkit*.js files. The GLG library loads a GLG drawing
// and renders it on a web page, providing an API to handle user interaction
// with graphical objects in the drawing.
//
// The drawings are created using the GLG Graphics Builder, an interactive
// editor that allows to create grahical objects and define their dynamic
// behavior without any programming. The airplane icons are dynamic
// GLG objects created with the Graphics Builder.
//
// The background map is generated by the GLG Map Server, which is integrated
// inside of a GLG drawing as a GLG GIS Object. The GIS Object takes a
// complete care of the map display, automatically generating Map Server
// requests to re-generate the image when the map is panned or zoomed.
//
// The demo uses asyncronous map loading: when the map is zoomed or scrolled,
// the drawing is changed right away, while the new map will appear when it's
// loaded. The GLG Air Traffic Control Demo demonstrates an alternative
// approach that uses an asynchronous map loading request to load a map:
// when the map is zoomed or scrolled, the program will continue to operate
// in the old zoom or pan state and will switch to the new zoom or pan state
// when the new map is ready. 
//
// Except for the changes to comply with the JavaScript syntax, this source
// is identical to the source code of the corresponding C/C++/C# and Java
// desktop versions of the demo.
//
// This demo uses the GLG Map Server to display a map.
// The Map Server has to be installed either on the local host or on a
// remote web server. After the Map Server has been installed, modify the
// source code to set the SuppliedMapServerURL variable to point to the
// Map Server location.
//////////////////////////////////////////////////////////////////////////////

/* eslint eqeqeq: 0 */

import { GlgToolkit } from '../GlgToolkitDemo.mod.js'

// Enable general debugging/diagnostics information.
const DEBUG = false;

/* Debugging: set the variable to true to throw an exception on a GLG error
   instead of just displaying an error message on the console.
*/
const DEBUG_GLG_ERRORS = false;

/* This demo uses the GLG Map Server to display a map.
   The Map Server has to be installed either on the local host or on a
   remote web server. After the Map Server has been installed, set
   SuppliedMapServerURL to point to the Map Server location:

   For example on Linux:
     SuppliedMapServerURL = "http://localhost/cgi-bin/GlmScript";
   or
     SuppliedMapServerURL = "http://www.myserver.com/cgi-bin/GlmScript";

   On Windows:
     SuppliedMapServerURL = "http://localhost/Scripts/GlmScript.pl";
   or
     SuppliedMapServerURL = "http://www.myserver.com/Scripts/GlmScript.pl";

   The SuppliedMapServerURL value overrides the URL of the GIS object in the
   drawing.
*/
const SuppliedMapServerURL = null;    /* String */

const UPDATE_INTERVAL = 50; // Update interval in msec.

const US_ZOOM_DELAY1 = 2000;  // Delay to zoom to the US area to show details.
const US_ZOOM_DELAY2 = 2000;  // Delay to remove US zooming message.

/* Controls the plane speed (in relative coordinates) used to simulate moving
   airplanes.
*/
const PLANE_SPEED_DESKTOP = 0.001;    // Desktop version
const PLANE_SPEED_MOBILE = 0.005;     // Mobile version

/* Controls the plane speed (in XY coordinates) used to simulate moving
   airplanes in the zoomed state.
*/
const MAX_ZOOM_SPEED = 5.0;

/* Scale factors used for changing airplane sizes. */
const
   SMALL_SIZE  = 0.23,
   MEDIUM_SIZE = 0.33,
   BIG_SIZE =    0.43;

// Global handle to the GLG Toolkit library.
let GLG = new GlgToolkit();

//////////////////////////////////////////////////////////////////////////////
// Creates an instance of the GIS demo.
// Parameters:
//   glg_div_name  - name of parent div the drawing will be displayed in,
//                   will be passed by the caller.
//   is_mobile     - true if deployed on mobile devices.
//   is_standalone - true if deployed in html, false if deployed in react or
//                   angular.
//   glg_path      - path to the directory where GLG drawings are located.
//////////////////////////////////////////////////////////////////////////////
export function GlgGIS( glg_div_name, glg_path, is_standalone, is_mobile )
{
   Debug( "New script for " + glg_div_name + ": "  +
          Object.getPrototypeOf( this ).constructor.name +
          " glg_path: " + glg_path );
   
   if( DEBUG_GLG_ERRORS )   
     GLG.ThrowExceptionOnError( true, true, true );
   else
     GLG.ThrowExceptionOnError( false, false, false );

   this.GLG_div_name = glg_div_name;
   this.IsMobile = is_mobile;
   this.Standalone = is_standalone;    

   // Use path to the drawings directory is supplied.
   this.GlgPath = glg_path;

   /* Defines an action to perform on touch on mobile devices: 
      select an airplane or scroll the map. On mobile devices, set to false
      to select a plane when it is touched by default. This may be changed
      using the Touch Action buttons.
   */
   this.ScrollMode = ( is_mobile ? false : true );

   /* Array of icons to place on the map as GLG objects in addition to the icons
      defined in GIS server's data. The icons that use GLG objects may be 
      selected with the mouse and their attributes can be changed dynamically, 
      based on data. When the mouse moves over an icon, it may be highlighted 
      with a different color or a tooltip may be displayed. 
   */
   this.NodeArray =
     [ new NodeData( "Boston",        -71.01789,  42.33602 ),
       new NodeData( "New York",      -73.97213,  40.77436 ),
       new NodeData( "San Francisco", -122.55478, 37.79325 ),
       new NodeData( "Miami",         -80.21084,  25.77566 ),
       new NodeData( "Seattle",       -122.35032, 47.62180 ),
       new NodeData( "Houston",       -95.38672,  29.76870 ),
       new NodeData( "Denver",        -104.87265, 39.76803 ),
       new NodeData( "Minneapolis",   -93.26684,  44.96185 ),
       new NodeData( "Chicago",       -87.68496,  41.83705 ),
       new NodeData( "Dallas",        -96.76524,  32.79415 ) ];

   this.NumNodes = this.NodeArray.length;  /* Number of airports */
   this.NumPlanes = 10;                 /* Number of simulated planes */    

   this.PlaneSpeed = ( is_mobile ? PLANE_SPEED_MOBILE : PLANE_SPEED_DESKTOP );

   this.Drawing = null;                 /* GlgObject */
   this.PositionObject = null;          /* GlgObject */
   this.PositionArea = null;            /* GlgObject */

   this.NodeTemplate = new Array( 2 );  /* GlgObject[2] */
   this.PlaneTemplate = new Array( 2 ); /* GlgObject[2] */
   this.Map = new Array( 2 );           /* GlgObject[2] */
   this.GISObject = new Array( 2 );     /* GlgObject[2] */
   this.NodeGroup = new Array( 2 );     /* GlgObject[2] */
   this.PlaneGroup = new Array( 2 );    /* GlgObject[2] */
   this.MapProjection = new Array( 2 ); /* int[2] */
   this.MapLoaded = new Array( 2 );     /* boolean[2] */

   // Stores initial extent and center, used to reset the map.
   this.InitExtent = new Array( 2 );    /* GlgPoint[2] */
   this.InitCenter = new Array( 2 );    /* GlgPoint[2] */

   this.PlaneSize = MEDIUM_SIZE;        /* double */

   /* If true, pan the map to make the selected plane visible in the current
      zoomed area when it moves out of the area.
   */
   this.LockSelectedPlane = false;     /* boolean */
   this.CityLabels = true;             /* boolean */
   this.StateDisplay = true;           /* boolean */
   this.SuspendPromptUpdates = false;  /* boolean */
   this.PanMode = false;               /* boolean */
   this.PerformUpdates = true;         /* boolean */
   this.DraggingFromButton = false;    /* boolean */

   // Temporary variables: allocate once
   this.lat_lon = GLG.CreateGlgPoint( 0, 0, 0 );          /* GlgPoint */
   this.position = GLG.CreateGlgPoint( 0, 0, 0 );         /* GlgPoint */
   this.old_position = GLG.CreateGlgPoint( 0, 0, 0 );     /* GlgPoint */
   this.new_position = GLG.CreateGlgPoint( 0, 0, 0 );     /* GlgPoint */
   this.last_xyz = GLG.CreateGlgPoint( 0, 0, 0 );         /* GlgPoint */
   this.curr_xyz = GLG.CreateGlgPoint( 0, 0, 0 );         /* GlgPoint */
   this.point = GLG.CreateGlgPoint( 0, 0, 0 );            /* GlgPoint */
   this.util_point = GLG.CreateGlgPoint( 0, 0, 0 );       /* GlgPoint */
   this.selection_rect = GLG.CreateGlgCube( null, null ); /* GlgCube */

   this.PlaneArray = null;             /* PlaneData[] */
   this.SelectedPlane = null;          /* PlaneData */

   /* Coefficient for canvas resolution. It will be adjusted in 
      SetCanvasResolution() for mobile devices with HiDPI displays as well as 
      on browser zoom.
   */
   this.CoordScale = 1;
}

////////////////////////////////////////////////////////////////////////////// 
// Script entry point: Starts GIS demo by loading its drawing.
////////////////////////////////////////////////////////////////////////////// 
GlgGIS.prototype.Start = function()
{
   Debug( "Starting: " + this.GLG_div_name );

   this.Active = true;
   
   // Set initial size of the drawing.
   this.SetDrawingSize( false );

   // Show touch action selector for mobile devices.
   if( this.IsMobile )
     ShowElement( this.GLG_div_name + "_touch_action", true );

   /* Load misc. assets such as GLG scrollbars. When assets are loaded, 
      LoadDrawing callback is invoked that loads a GLG drawing defined by
      DrawingName variable.
   */
   this.LoadAssets( ()=>this.LoadDrawing(), null );
}

////////////////////////////////////////////////////////////////////////////// 
// Script entry point: Performs cleanup.
////////////////////////////////////////////////////////////////////////////// 
GlgGIS.prototype.Cleanup = function()
{
   Debug( "Cleanup for: " + this.GLG_div_name );
   
   this.Active = false;    // Ignore any pending updates and callbacks.

   if( this.Drawing )
     this.Drawing.ResetHierarchy();   // Undisplay GLG drawing.

   if( this.ResizeListener )
     window.removeEventListener( "resize", this.ResizeListener );
}

////////////////////////////////////////////////////////////////////////////// 
// Load a GLG drawing from a file.
////////////////////////////////////////////////////////////////////////////// 
GlgGIS.prototype.LoadDrawing = function()
{
   Debug( "LoadDrawing for: " + this.GLG_div_name );
   
   if( !this.Active )
     return;

   /* Load a drawing from the gis_demo.g file. 
      The LoadCB callback will be invoked when the drawing has been loaded.

      Using "bind( this )" as a shorter way to provide "this" compared with 
      using lambda: with bind, we do not need to specify parameter list that
      we would need to provide for lambda.
   */
   let drawing_file = ( this.IsMobile ? "gis_demo2.g" : "gis_demo.g" );   

   GLG.LoadWidgetFromURL( this.GetFullName( drawing_file ), null,
                          this.LoadCB.bind( this ), /*user data*/ null,
                          /*abort test function*/ ()=>!this.Active );
}

////////////////////////////////////////////////////////////////////////////// 
GlgGIS.prototype.GetFullName = function( drawing_name )
{
   if( this.GlgPath == null )
     return drawing_name;

   return this.GlgPath + "/" + drawing_name;
}

//////////////////////////////////////////////////////////////////////////////
GlgGIS.prototype.LoadCB =
  function( /*GlgObject*/ drawing, /*Object*/ user_data, /*String*/ path )
{
   Debug( "LoadCB for: " + this.GLG_div_name );

   if( !this.Active )
     return;

   if( drawing == null )
   {
      AppAlert( "Can't load drawing, check console message for details." );
      return;
   }
   
   if( !document.getElementById( this.GLG_div_name ) )
   {
      Debug( "Can't find " + this.GLG_div_name +
             " div: it may have been removed from the document." );
      return;
   }
    
   // Disable spinning loader.   
   RemoveElement( this.GLG_div_name, "loader_container" );
    
   // Define the element in the HTML page to display the drawing.
   drawing.SetParentElement( this.GLG_div_name );
    
   // Disable viewport border to use the border of the glg_area.
   if( this.Standalone )
     drawing.SetDResource( "LineWidth", 0 );
    
   this.StartGISDemo( drawing );
}

//////////////////////////////////////////////////////////////////////////////
GlgGIS.prototype.StartGISDemo = function( drawing )
{
   this.Drawing = drawing;

   this.InitDrawing();
   this.InitIcons();

   this.Drawing.SetupHierarchy();

   /* Adjust selected region on the thumbnail map to match the zoomed area 
      of the detailed map.
   */
   this.SetSelectedArea();

   this.SetMapLoading( 0, true );
   this.SetMapLoading( 1, true );

   this.Drawing.Update();
   
   // Start animation.
   setTimeout( ()=>this.UpdatePlanes(), UPDATE_INTERVAL );

   // Zoom to the US area after a few seconds to show details.        
   setTimeout( ()=>this.ZoomToUSStart(), US_ZOOM_DELAY1 );
}

//////////////////////////////////////////////////////////////////////////////
GlgGIS.prototype.InitDrawing = function()
{
   // Show mouse over tooltips right away.
   this.Drawing.SetDResource( "$config/GlgMouseTooltipTimeout", 0.05 );

   this.Map[ 0 ] = this.Drawing.GetResourceObject( "TopMap" ); // Thumbnail map
   this.Map[ 1 ] = this.Drawing.GetResourceObject( "Map" );    // Detailed map

   /* Display thumbnail map in a floating dialog at run time. It is kept 
      as a child viewport in the drawing for the convinience of editing.
   */
   this.Map[ 0 ].SetDResource( "ShellType", GLG.GlgShellType.DIALOG_SHELL );
   this.Map[ 0 ].SetSResource( "Screen/ScreenName", "Globe View" );

   // Set an initial position of the thumbnail map dialog.
   if( this.IsMobile )
     this.Map[ 0 ].SetGResource( "Position", 800, -1600, 0 );
   else
     this.Map[ 0 ].SetGResource( "Position", 50, -1120, 0 );        
   
   for( let i=0; i<2; ++i )   // For each map window
   {
      // Get IDs of the GIS objects in each of the map viewports. 
      this.GISObject[ i ] = this.Map[ i ].GetResourceObject( "GISObject" );

      if( SuppliedMapServerURL != null )
        // Override the URL defined in the drawing.
        this.GISObject[ i ].SetSResource( "GISMapServerURL",
                                          SuppliedMapServerURL );

      /* Query and store the GIS projection (ORTHOGRAPHIC or RECTANGULAR)
         used to render the map.
      */
      this.MapProjection[ i ] =
        Math.trunc( this.GISObject[ i ].GetDResource( "GISProjection" ) );

      /* Set the GIS Zoom mode. It was set and saved with the drawing, 
         but do it again programmatically just in case.
      */
      this.Map[ i ].SetZoomMode( null, this.GISObject[ i ], null, 
                                 GLG.GlgZoomMode.GIS_ZOOM_MODE );

      // Store initial map extent for resetting after zooming.
      this.InitExtent[i] = this.GISObject[ i ].GetGResource( "GISExtent" );
      this.InitCenter[i] = this.GISObject[ i ].GetGResource( "GISCenter" );
   }

   /* Add Input and Trace callbacks used to handle user interaction. */
   this.Drawing.AddListener( GLG.GlgCallbackType.INPUT_CB,
                             this.InputCallback.bind( this ) );
   this.Drawing.AddListener( GLG.GlgCallbackType.TRACE_CB,
                             this.TraceCallback.bind( this ) );
}

//////////////////////////////////////////////////////////////////////////
// Initializes icons in the drawing
//////////////////////////////////////////////////////////////////////////
GlgGIS.prototype.InitIcons = function()
{
   // Get the palette containing templates for plane and node icons.
   let palette = this.Drawing.GetResourceObject( "Palette" );

   // Delete it from the drawing
   this.Drawing.DeleteThisObject( palette );

   /* Get node and plane templates from the palette. Two sets of templates
      are used: smaller icons for the thumbnail view and more elaborate 
      ones for the detailed map.
   */
   for( let i=0; i<2; ++i )
   {
      this.NodeTemplate[ i ] =
        palette.GetResourceObject( i == 0 ? "Node1" : "Node2" );

      this.PlaneTemplate[ i ] = 
        palette.GetResourceObject( i == 0 ? "Plane1" : "Plane2" );

      /* If the icon is not a marker (IconScale resource exists), set the
         icon's size.
      */
      let resource = this.PlaneTemplate[ i ].GetResourceObject( "IconScale" );
      if( resource != null )
        resource.SetDResource( null, this.PlaneSize );
   }

   // Create and initialize plane structures used for simulation.
   this.PlaneArray = new Array( this.NumPlanes );
   for( let i=0; i < this.NumPlanes; ++i )
   {
      this.PlaneArray[ i ] = new PlaneData();
      this.PlaneArray[ i ].name = i.toString();
   }
      
   // Add node and plane icons to both thumbnail and detailed map.
   for( let i=0; i<2; ++i )
   {	 
      this.CreateAirportIcons( i ); // Add airport icons
      this.CreatePlaneIcons( i );   // Add plane icons
   }

   // Start all planes.
   for( let i=0; i < this.NumPlanes; ++i )
     this.StartPlane( this.PlaneArray[ i ], true );

   /* Selected area annotates the currently viewed area of the detailed map 
      in the thumbnail map view. Reorder SelectedArea on top of icons
      (last in the array).
   */
   let selected_area =   /* GlgObject */
     this.GISObject[ 0 ].GetResourceObject( "GISArray/SelectedArea" );
   this.GISObject[ 0 ].ReorderElement( this.GISObject[ 0 ].GetIndex( selected_area ),
                                       this.GISObject[ 0 ].GetSize() - 1 );

   /* Set state display on the thumbnail map. Airport labels on the detailed
      map are handled by HandleZoomLevel(). 
   */
   this.SetGISLayers( 0 );

   /* Demos starts with the whole word view, then zooms to the US area
      in a few seconds to show more details. Set initial parameters
      for the whole world view.
   */
   this.HandleZoomLevel();

   this.InitSelection();  // Select some plane.

   // Store objects used to display lat/lon on mouse move.
   this.PositionArea = this.Drawing.GetResourceObject( "PositionArea" );
   this.PositionObject =
     this.Drawing.GetResourceObject( "PositionLabel/PointerLatLon" );
   this.PositionObject.SetSResource( null, "" );

   // Set US zooming message to OFF initially.
   this.Drawing.SetDResource( "Map/USZoomingMessage/Visibility", 0.0 );
}

//////////////////////////////////////////////////////////////////////////
// Creates NodeGroup to hold airport icons and adds the icons to it.
//////////////////////////////////////////////////////////////////////////
GlgGIS.prototype.CreateAirportIcons = function( /* int */ map )
{
   this.NodeGroup[ map ] =
     GLG.CreateObject( GLG.GlgObjectType.ARRAY,
                       GLG.GlgContainerType.GLG_OBJECT, 0, 0, null );
   this.NodeGroup[ map ].SetSResource( "Name", "NodeGroup" );

   // Add city/node icons
   for( let i = 0; i < this.NumNodes; ++i )
     this.AddNode( this.NodeArray[ i ], map, i );

   /* Add the group to the GIS Object which will automatically manage 
      the GIS coordinate conversion. This allows to specify node 
      positions in lat/lon instead of the X/Y world coordinates.
   */
   this.GISObject[ map ].AddObjectToBottom( this.NodeGroup[ map ] );
}

//////////////////////////////////////////////////////////////////////////
// Creates PlaneGroup to hold plane icons and adds the icons to it.
//////////////////////////////////////////////////////////////////////////
GlgGIS.prototype.CreatePlaneIcons = function( /* int */ map )
{
   this.PlaneGroup[ map ] =
     GLG.CreateObject( GLG.GlgObjectType.ARRAY,
                       GLG.GlgContainerType.GLG_OBJECT, 0, 0, null );
   this.PlaneGroup[ map ].SetSResource( "Name", "PlaneGroup" );

   // Add plane icons
   for( let i=0; i < this.NumPlanes; ++i )
     this.AddPlane( this.PlaneArray[ i ], map, i );
   
   /* Add the group to the GIS Object which will automatically manage 
      the GIS coordinate conversion. This allows to specify plane 
      positions in lat/lon instead of the X/Y world coordinates.
   */
   this.GISObject[ map ].AddObjectToBottom( this.PlaneGroup[ map ] );
}

//////////////////////////////////////////////////////////////////////////
GlgGIS.prototype.AddNode =
  function( /* NodeData */ node_data, /* int */ map, /* int */ index )
{      
   // Create a copy of a node (GlgObject). */
   let node =
     this.NodeTemplate[ map ].CloneObject( GLG.GlgCloneType.STRONG_CLONE );
      
   node.SetSResource( "Name", node_data.name );  // Set object name

   /* Set node position in lat/lon coordinates. The GIS object will handle
      the GIS coordinate conversion depending on the map the node icon is 
      displayed in, as well as the map's zoom and pan state.
   */
   node.SetGResource( "Position", 
                      node_data.lat_lon.x, node_data.lat_lon.y, 0.0 );

   // Index for direct access
   node.SetDResource( "DataIndex", index );

   if( map == 1 )        // On the detailed map, show node name label.
     node.SetSResource( "LabelString", node_data.name );
      
   let tooltip;
   if( map == 0 )   // On the thumbnail map, show node name in the tooltip.
     tooltip = node_data.name;
   else   // On the detailed map, include lat/lon into the tooltip.
     tooltip =
       node_data.name + ", " + CreateLocationString( node_data.lat_lon );
   node.SetSResource( "TooltipString", tooltip );
        
   node_data.graphics[ map ] = node;

   // Add the node to the requested map (thumbnail or detailed).
   this.NodeGroup[ map ].AddObjectToBottom( node );
}

//////////////////////////////////////////////////////////////////////////
// Adds a plane icon, fills labels, tooltips, etc.
//////////////////////////////////////////////////////////////////////////
GlgGIS.prototype.AddPlane =
  function( /* PlaneData */ plane_data, /* int */ map, /* int */ index )
{
   // Create a copy of a plane (GlgObject).
   let plane =
     this.PlaneTemplate[ map ].CloneObject( GLG.GlgCloneType.STRONG_CLONE );
   
   plane.SetSResource( "Name", plane_data.name );      // Object name
   
   // Index for direct access
   plane.SetDResource( "DataIndex", index );
   
   plane_data.graphics[ map ] = plane;
   
   // Check if the icon has an angle to indicate its direction.
   if( plane.GetResourceObject( "Angle" ) != null )
     plane_data.has_angle[ map ] = true;
   
   /* The plane will be positioned with PositionPlane() after the GIS object
      have been setup and the plane's lat/lon has been calculated by the 
      flight simulation. */

   // Add the plane to the requested map (thumbnail or detailed).
   this.PlaneGroup[ map ].AddObjectToBottom( plane );
}

//////////////////////////////////////////////////////////////////////////
GlgGIS.prototype.PositionPlane =
  function( /* PlaneData */ plane, /* int */ map )
{
   if( plane.graphics[ map ] == null || 
       plane.from_node == null || plane.to_node == null )
     return;
   
   // Obtain the plane's current position.
   GetPlaneLatLon( plane );

   /* Update plane's icon in the drawing by setting its lat/lon coordinates.
      The GIS object will handle the GIS coordinate conversion depending on 
      the map the plane icon is displayed in, as well as the map's zoom 
      and pan state.
   */
   plane.graphics[ map ].SetGResource( "Position",
                                       plane.lat_lon.x, plane.lat_lon.y, 0.0 );

   // Update icon's direction angle is necessary
   if( plane.has_angle[ map ] )
   {
      let angle = this.GetPlaneAngle( plane, map );   /* double  */
      plane.graphics[ map ].SetDResource( "Angle", angle );
   }
}

//////////////////////////////////////////////////////////////////////////
// FOR FLIGHT SIMULATION ONLY: Converts plane lat/lon to the GLG world 
//     coordinates for calculating plane speed and directional angle.
//////////////////////////////////////////////////////////////////////////
GlgGIS.prototype.GetPlanePosition =
  function( /* PlaneData */ plane, /* int */ map, /* GlgPoint */ xyz )
{
   // Converts lat/lon to X/Y using GIS object's current projection.
   this.GISObject[ map ].GISConvert( null, GLG.GlgCoordType.OBJECT_COORD, 
                                     /* Lat/Lon to XY */ false,
                                     plane.lat_lon, xyz );
}

//////////////////////////////////////////////////////////////////////////
// FOR FLIGHT SIMULATION ONLY: Converts node lat/lon to the GLG world 
//     coordinates for calculating plane's initial directional angle.
//////////////////////////////////////////////////////////////////////////
GlgGIS.prototype.GetNodePosition =
  function( /* NodeData */ node, /* int */ map, /* GlgPoint */ xyz )
{
   // Converts lat/lon to X/Y using GIS object's current projection.
   this.GISObject[ map ].GISConvert( null, GLG.GlgCoordType.OBJECT_COORD, 
                                     /* Lat/Lon to XY */ false,
                                     node.lat_lon, xyz );
}

//////////////////////////////////////////////////////////////////////////
// FOR FLIGHT SIMULATION ONLY: Calculates plane icon's directional angle.
//     In an application, it will query the plane's directional angle.
//////////////////////////////////////////////////////////////////////////
GlgGIS.prototype.GetPlaneAngle =
  function( /* PlaneData */ plane, /* int */ map )  /* double */
{
   /* Rectangular projection preserves straight lines, we can use the 
      angle of the line connecting the start and end nodes. For the
      orthographic projection, use this case if the plane has just started
      and there is no previous position stored.
   */
   if( this.MapProjection[ map ] == GLG.GlgProjectionType.RECTANGULAR_PROJECTION ||
       plane.path_position == plane.path_position_last )   // Just started
   {
      this.GetNodePosition( plane.from_node, map, this.last_xyz );
      this.GetNodePosition( plane.to_node, map, this.curr_xyz );
   }
   else  /* In the orthographic projection straight lines are drawn as 
            curves. Use the angle of the line connecting the current and 
            last position of the plane. */
   {
      let stored_position;   /* double */
      
      stored_position = plane.path_position;    // Store current position.
      
      // Get coordinates of the plane's previous position
      plane.path_position = plane.path_position_last;
      GetPlaneLatLon( plane );
      this.GetPlanePosition( plane, map, this.last_xyz );
      
      // Restore the plane's current position and get its coordinates.
      plane.path_position = stored_position;
      GetPlaneLatLon( plane );
      this.GetPlanePosition( plane, map, this.curr_xyz );
   }

   /* Calculate the angle of a line connecting the previous and 
      current position.
   */
   return GetAngle( this.last_xyz, this.curr_xyz );
}

//////////////////////////////////////////////////////////////////////////
// Checks if the object is visible in the current zoom region.
// This prevents wrap-around errors under big zoom factors.
//////////////////////////////////////////////////////////////////////////
function GetVisibility( /* GlgPoint */ position,
                        /* double */ adj )   /* boolean */
{
   // Use adj as a gap
   return ( position.x > -1000.0 * adj && position.x < 1000.0 * adj &&
            position.y > -1000.0 * adj && position.y < 1000.0 * adj );
}

