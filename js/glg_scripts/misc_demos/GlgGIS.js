//////////////////////////////////////////////////////////////////////////
function RELATIVE_TO_NEW_RANGE( /* double */ low, /* double */ high,
                                /* double */ rel_value )   /* double */
{
   return ( low + ( high - low ) * rel_value );
}

//////////////////////////////////////////////////////////////////////////
function RadToDeg( /* double */ angle )   /* double */
{
   return angle / Math.PI * 180.0;
}

