   this.flight_number = 0;          /* int */
   this.tooltip = new Array( 2 );   /* String[2] */
   this.graphics = new Array( 2 );  /* GlgObject[2] */
   this.from_node = null;           /* NodeData */
   this.to_node = null;             /* NodeData */
