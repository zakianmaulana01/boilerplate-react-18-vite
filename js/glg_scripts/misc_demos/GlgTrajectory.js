//////////////////////////////////////////////////////////////////////////
// GIS request observer interface: receives status update notifications.
//////////////////////////////////////////////////////////////////////////
GlgTrajectory.prototype.RequestUpdate = function( /* GlgObject */ gis_object, status )
{
   switch( status )
   {
    case GLG.GlgGISRequestStatus.GIS_REQUEST_READY:
      if( this.GISObject.InstallGISRequest( null ) )
      {
         this.Map.Update();

         /* Query the new center of the GIS projection. */
         this.GISCenter = this.GISObject.GetGResource( "GISCenter" );
      }
      else
        AppLog( "Failed to install map request." );
      break;
      
    default:
      AppLog( "Aborting previous GIS request." ); 
      break;
   }

   this.SetMapLoading( false );
}
